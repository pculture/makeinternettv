<h1 class="pagetitle">Chapter 6.3a: Know Your Feed</h1>

<p>This topic may seem complex, but stick with us &mdash; we'll try to keep it as simple as possible. Most likely, you already have a feed generated by your blog or video service &mdash; we'll help you find it.</p>

<p>A "media feed", more commonly known as a podcast (other names you may recognize: RSS, Atom, etc.), allows your viewers to get audio or video <a class="wikilink" href="http://www.youtube.com/watch?v=y-MSL42NV3c">delivered directly to their computers</a> using programs like iTunes, Adobe Media Player, and Miro.</p>

<p>Feeds are a fantastic way to keep your audience up to date automatically &mdash; we consider them the heart of a decentralized internet TV system.</p> 

<p>First you'll need to locate your feed URL, and then we'll show you some great ways to use it.</p>

