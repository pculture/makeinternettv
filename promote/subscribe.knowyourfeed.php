<h2 id="blogfeedfinder">If You Use a Video Blog:</h2>
<div class="details">
<p><a class="wikilink" href="http://www.mozilla.com/firefox/">Firefox 3</a> is a very useful tool for locating your feed; we recommend you download and install it. Load up the front page of your blog and look in the address bar for a blue feed icon. If you click on that blue icon, a URL will appear in the address bar and your posts should pop into the page. Copy that URL and then make sure you've got media in your feed (see below).</p>

<img src="images/promote/firefox-icon.jpg" alt="firefox-icon" />

</div> <!-- END DETAILS -->
<br />
</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Make Sure You've Got a Media Feed</h2>
<p>Not all feeds are media enabled, which means the viewer won't get any video. When Firefox 3 loads up your feed, each post should have a little grey box below it that says "Media files" and a link to your video file (see image below). </p>

<img src="images/promote/firefox-feed.jpg" alt="firefox-feed" />
<br /><br />

<p>If you don't have links in your feed, we recommend putting a hyperlink to your video file (.wmv, .mov, .mp4, etc) each time you post a new video (just add the link to the blog post). After that, follow our instructions for <a href="publish/blog.feed.php#burnafeed">burning your existing feed</a>.

<p>We think media feeds are <em>so important</em> that we've written an <a class="wikilink" href="http://www.getmiro.com/articles/future_of_video.php">essay</a> on the subject!</p>
