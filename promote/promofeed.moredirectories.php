<h1>Alternative Directories</h1>
<div class="details">
<p>These directories are all web based, meaning that a person can visit these directories straight from their browsers.</p>
</div> <!-- END DETAILS -->	
<br style="margin-bottom:1em;" />
<img class="title border" src="images/promote/promofeed/mefeedia.jpg" alt="Mefeedia" />
<div class="details">
<p><a target="_blank" href="http://mefeedia.com/">Mefeedia</a> is a massive directory of video RSS feeds. They have been around for quite a while (by video blogging standards), and have some cool features for users browsing, organizing and sharing their favorite videos and feeds.</p>
<p><a target="_blank" href="http://mefeedia.com/add.php">Submit your RSS feed to Mefeedia</a>.</p>
</div> <!-- END DETAILS -->	
<br style="margin-bottom:1em;" />
<img class="title border" src="images/promote/promofeed/vlogbot.jpg" alt="VlogBot" />
<div class="details">
<p><a target="_blank" href="http://vlogbot.net/">VlogBot</a> is another guide with lots of videos and video blogs catalogued and categorized. Submitting your feed is incredibly easy.</p>
<p><a target="_blank" href="http://vlogbot.net/add">Submit your RSS feed to VlogBot</a>.</p>
</div> <!-- END DETAILS -->	
