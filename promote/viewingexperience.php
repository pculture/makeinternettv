<h2>Streaming Video:</h2>

<img class="lined" src="images/promote/viewingstreaming.jpg" alt="Streaming Video" />
<br /><br />
<p>Streaming happens when you press a video's play button, and it begins playing while it's still downloading. It's common on sites like YouTube and blip.tv. Streaming lets viewers instantly and casually watch your videos.</p> 

<p>If you host your videos on a sharing site, they should be able to both stream and get embedded on your blog or website (see below). If you self-host a DIY Wordpress blog, you can set up streaming with <a class="wikilink" href="http://showinabox.tv/">Show in a Box</a>.</p>
</div> <!-- END DETAILS -->

<div class="detailz clearfix">
<h2>Embedding Video:</h2>
<img class="left lined" src="images/promote/tubeembed.jpg" style="margin:0 15	px 10px 0;" />
<p>Embedding is putting a video from one site onto a different site, using just a little bit of HTML code. It allows bloggers, friends, famous people, and your grandma to easily add your video to their website. All embedded videos can be streamed (see above).</p> 

<p><strong>Embedding from a video sharing site:</strong> locate the block of embeddable html code. It's usually on the video page, or sometimes inside the video player itself &mdash; the code usually begins with <em>&lt;object...</em> or <em>&lt;embed...</em>. Once you've found it, copy/paste the code into your blog post, website, or social networking page. Make sure to test the page before you publish.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Embedding can Leave a Single Point of Failure</h2>
<img class="lined right" src="images/promote/tosviolation.jpg" alt="terms of service violation" />
<p>Embedding is a totally amazing way to spread video, but it is somewhat vulnerable. Let's say a bunch of your friends embed your video, and the original gets taken down for some reason &mdash;perhaps a <a class="wikilink" href="http://www.getmiro.com/blog/2008/10/mccain-campaign-witnesses-dark-side-of-dmca/">bogus copyright claim</a>&mdash; every embedded version of the video simultaneously stops working. In other words, no one anywhere can see your video.</p>

<p>Pushing your audience to a blog or website that is separate from your video hosting service can minimize the risk of a suspended or revoked account. This way, your videos can be hosted and/or re-uploaded from any other sharing site, without <a class="wikilink" href="http://www.kevindriscoll.info/todomundo/2008/12/29/youtube-video-removed-dozens-of-comments-lost/">losing any of your hard earned audience</a>.</p>

</div> <!-- END CALLOUT -->

<div class="interior">

<div class="detailz clearfix">
<h2>Downloading Video:</h2>
<img class="right lined" src="images/promote/download.jpg" style="margin:0 15px 10px 0;" />
<p>Making your video available for download allows your audience even more flexibility in how/where they watch your video. Downloading is especially useful for: high-definition, podcasting, superior subscription services. You'll learn more about the benefits of syndication and downloading in the next section.</p>
<p>Some video sharing sites, for example YouTube, don't encourage downloading, others, like <a href="./publish/service.blip.php">blip.tv</a>, do let you offer downloads.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Why Let Them Download?</h2>
<p>It may seem scary to let people download your videos. There are two big reasons to face this fear. The first: if you put it online, a savvy individual can capture, download, and remix/repost/repurpose your video, no matter how many locks or limits you put on it. Second: downloads can deliver a great user experience, for example your audience can subscribe with <a class="wikilink" href="http://getmiro.com">Miro</a>. You might as well give your audience the best experience possible.</p>

</div> <!-- END CALLOUT -->

