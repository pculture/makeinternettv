<h1>Getting into the Miro Guide</h1>
<img class="lined" src="images/promote/promofeed/miro-guide.gif" alt="Miro Directory" />
<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Make a Miro Guide account <a target="_blank" href="https://www.miroguide.com/channels/submit">here</a>.</p>
<img class="steps" src="images/promote/promofeed/miro-01.gif" alt="Create an Account" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Submit Your Feed</h2>
<p>Once you're logged in, visit the <a target="_blank" href="https://channelguide.participatoryculture.org/channels/submit">Submission Page</a>.</p>
<img class="steps" src="images/promote/promofeed/miro-02.gif" alt="Enter your Feed" />
<p>Paste in your feed address &mdash; if you don't remember what this is, check out <a href="Chapter 6.2: Promote Your Feed">Chapter 6.2: Promote Your Feed</a>.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Fill in the Blanks</h2>
<p>Many of these fields are automatically filled in, just fill in the blanks.</p>
<img class="steps" src="images/promote/promofeed/miro-03.gif" alt="More Info" />

<p>Your channel logo is very important, as people will often subscribe to a channel because it has an interesting logo. We recommend using a 250px by 188px image (or something with a 4:3 aspect ratio).</p>

<img class="steps" src="images/promote/promofeed/miro-04.gif"alt="Upload Image" />


<p>Once you're done filling in the blanks, click <strong>Next Step</strong>.</p>

<img class="steps" src="images/promote/promofeed/miro-05.gif"alt="Submit" />

<p>The PCF's volunteer moderation team will review and approve your submission. It's a good idea to test your RSS feed manually, in Miro, <em>before</em> submitting it to the channel guide.</p>

</div> <!-- END STEP 3 -->

