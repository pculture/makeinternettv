<h2>RSS versus Media RSS</h2>
<p>Some video sharing sites only offer plain RSS feeds that <em>don't</em> include media links.</p>
<p>The sites we've recommended all offer full Media RSS feeds, which is great because it offers your viewers more flexibility in how they watch your videos.</p>
<p>We think media RSS is <em>so important</em> that we've written an <a target="_blank" href="http://www.getmiro.com/articles/future_of_video.php">essay</a> on the subject!</p>
