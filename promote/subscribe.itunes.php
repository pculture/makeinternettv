<h1 class="pagetitle">Chapter 6.3c: Syndicate to the iTunes Store</h1>
<p>The iTunes store (only accessible via the <a class="wikilink" href="http://www.apple.com/itunes/overview/">iTunes</a> application) is a directory full of podcasts. In the podcasting section, there is a sub-section named "Video Podcasts" &mdash; this is where you submit your iTunes compatible video feed.</p> 

<p>Despite the video podcast section being fairly hidden, iTunes Store has a lot of users searching and browsing the videosÂ &mdash; so it's worthwhile to get listed</p>

<p>Be aware that all video feeds in the iTunes store must be comprised of Quicktime compatible videos (no YouTube, no Flash video). You'll want to test your videos in Quicktime and/or iTunes beforehand, to make sure they work.</p>
