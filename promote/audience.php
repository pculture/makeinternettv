<h1>Three Types of Viewer</h1>
<div class="detailz clearfix">
<img class="right illus" src="images/illustrations/promote_laptop.png" style="margin:0 0 10px 5px;" />
<h2>Casual Viewer:</h2>
<p>This person stumbles upon your video, and watches it. Finding/attracting this person is all about making your videos easy to watch, and getting them in front of as many eyeballs as possible. Strategies include: posting to multiple hosting services (check out: <a href="publish/service.tubemogul.php">TubeMogul</a>), commenting on other peoples' videos (via text or video comment), sharing on social networks, or even just <a class="wikilink" href="http://www.insidehighered.com/news/2007/02/07/web">emailing friends</a>.</p> 

<p>The casual viewer is important, but ultimately you'll want this person to become a repeat viewer (aka a Site Visitor). Just remember to help the casual viewer back to your website or sharing account &mdash; a URL on your video is good, but a clickable hyperlink is best.</p>
</div> <!-- END DETAILS -->

<div class="detailz clearfix">
<img class="left illus" src="images/illustrations/promote_laptop.png" style="margin:0 15px 10px 0;" />
<h2>Site Visitor:</h2>
<p>These people know where your website or video page is, have watched some of your videos, and are ready to come back for more. They're definitely interested in your work and probably want more of the following: fresh and engaging videos, conversation, community, a better viewing experience, and/or contact with the creator (you!).</p> 

<p>Making new and compelling videos is probably the single most important way to keep site visitors coming back. Ultimately, you'll want them to subscribe to your videos, so they don't need to remember to come back.</p>
</div> <!-- END DETAILS -->

<div class="detailz clearfix">
<img class="right illus" src="images/illustrations/promote_laptop.png" style="margin:0 0 10px 5px;" />
<h2>Subscriber:</h2>
<p>The subscriber is your bread and butter &mdash; this is because you have a direct connection with them. When you publish, they know. No remembering to visit a site or anything; they are automatically notified and/or have your video pre-delivered and ready to watch. It's the way that internet TV should work!</p>

<p>There are many ways for people to subscribe to your work, but not all are equal &mdash; we'll explain some of the differences and help you score the best type, later in the chapter.</p> 


</div> <!-- END DETAILS -->

