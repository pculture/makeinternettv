<h1 class="pagetitle">Chapter 6.3b: Miro: Internet TV</h1>
<p><a class="wikilink" href="http://getmiro.com">Miro</a> is an application for subscribing to video feeds, organizing lots of video, and finding/watching/saving almost any video available on the internet (especially HD stuff).</p>

<p>Miro is free and open source software, and includes the Miro Guide &mdash; an open directory of thousands of video feeds and shows. Any creator can add their videos to the guide.</p>

<p style="font-size:95%;">Note: The Participatory Culture Foundation, a 501c3 non-profit organization, built this website and actively develops the Miro project.
