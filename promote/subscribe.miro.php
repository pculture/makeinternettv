<br /> <br />
<p>Miro is a free and open source internet TV application that works for Mac, Windows, and Linux. It's great for subscribing to feeds, especially when the video is in HD. You can <a class="wikilink" href="http://getmiro.com">download it here</a>.</p>

<h2>The Miro Guide</h2>
<img class="lined" src="images/promote/miroguide.jpg" alt="Miro Directory" />
<br /> <br />
<p>The Miro Guide is an open content directory that comes with Miro. It is also accessible via web browser: <a class="wikilink" href="http://miroguide.com">MiroGuide.com</a>.</p>
<h2>How to Get Your Feed in the Miro Guide</h2>
<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Go to the <a class="wikilink" href="https://www.miroguide.com/">Miro Guide</a> and click "Register Now!" under the Account tab.</p>
<img class="steps" src="images/promote/createaccount.jpg" alt="Create an Account" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Submit Your Feed</h2>
<p>Once you're logged in with an account, hover on your username (where the Account tab was) and then click "Submit a Feed".</p>
<img class="steps" src="images/promote/submitfeed.jpg" alt="Enter your Feed" />
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> I am the Creator</h2>
<p>Paste your <a href="./publish/subscribe.knowyourfeed.php">RSS feed</a> into the blank space and click "I am the Creator".</p>
<img class="steps" src="images/promote/adddialog.jpg" alt="Add Dialog" />

</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Fill in the Blanks</h2>
<p>Many of these fields are automatically filled in, just fill in the blanks.</p>
<img class="steps" src="images/promote/mirodeets.jpg" alt="More Info" />

<p>Your channel logo is very important, as people will often subscribe to a channel because it has an interesting logo. We recommend using a 300px by 200px image (or something with a 3:2 aspect ratio).</p>

<img class="steps" src="images/promote/moredeets.jpg"alt="More Details" />


<p>Once you're done filling in the blanks, click <strong>Submit Channel</strong>.</p>

<p>The PCF's volunteer moderation team will review and approve your submission. It's a good idea to test your RSS feed manually, in Miro, <em>before</em> submitting it to the channel guide.</p>

</div> <!-- END STEP 4 -->

