<h1 class="pagetitle">Chapter 6.3: Engage the Network</h1>
<p>As an internet video publisher, you can interact, and sometimes collaborate, with both your audience and your peers in a lot of interesting ways.</p>

<p>Conversations and links are the currency of the text blogging world, and it's not so different with video. Engaging a fellow video blogger in a conversation is a good way to drive traffic back to your video blog.</p>

