<h1>Finding Your Peers</h1>
<div class="details">
<p>Being a new video creator is just like being the new kid at school &mdash; you'll want to make a few friends and then branch out from there. Or maybe your style is beating up a bully, in order to win some allies (this is just a metaphor, don't think about it too hard).</p> 
<p>Ultimately, you're probably going to want a larger audience and more participation. The key is generating interest and engaging viewers and fellow creators.</p>

</div> <!-- END DETAILS -->


<h1>Start a Conversation</h1>
<div class="details">
<p>There are a lot of ways to start a conversation with a fellow video blogger, the simplest being a comment on a video post &mdash; just make sure you include a link back to your video blog (there is often a specific space for this).</p>
</div> <!-- END DETAILS -->

<h1>Video Responses</h1>
<div class="details">
<p>You can also create a video in response to another video blogger's post. There are a few ways to do this, one being to post a video response in your own blog and then leave a comment under the original video with a link back to your response. Some video services allow you to specifically post video responses as comments.</p>

<h1>Send Viewers Away for More Traffic</h1>
<p>Sharing and recommending good things (videos, websites, whatever) keeps people coming back for more; it also has the possible side effect of generating mutual traffic between websites or video creators.</p>
<p>It might seem like sending viewers away from your site (to a potential competitor) is a bad idea, but in reality, it's a common mechanism for bubbling the best of the web up to the top.</p>
</div> <!-- END DETAILS -->

<h1>Tracking Conversations</h1>
<div class="details">
<p>Most blogs and video services have the ability to give and receive comments. One nice thing about blogs in particular is the ability to give and receive <em>pings</em>.</p> 
<p>A <a class="wikilink" href="http://en.wikipedia.org/wiki/Ping_blog">ping</a> is created when someone else creates a blog post that links to one of your blog posts. This is a great way to know when people are talking about your work &mdash; you can see who said what, and respond in their comments.</p>

</div> <!-- END DETAILS -->


