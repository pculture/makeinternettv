<h2>Miro 1-Click Subscribe Button</h2>


<div class="centered">
<img src="images/promote/1clickmiro.png" alt="1-click Miro" />
<p>Miro 1-Click Subscription</p>
</div> <!-- END CENTERED -->

<div class="details">
<p>Use the <a class="wikilink" href="http://subscribe.getMiro.com/">Miro 1-Click Button Maker</a> to create a button for your site &mdash; jast paste your video feed URL and then you will get code for a personalized button. Just paste the HTML into your site.</p> 
