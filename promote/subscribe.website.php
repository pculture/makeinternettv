<h2>1-Click iTunes</h2>
<div class="details">
<p>iTunes <em>only</em> works for video feeds that are Quicktime compatible. To check your feed for compatibility, open iTunes and choose <strong>Advanced > Subscribe to Podcast</strong> and paste in your URL. Make sure you can download a video from your feed and play it back.</p>
</div> <!-- END DETAILS -->	

<div class="centered">
