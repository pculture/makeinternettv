<h1 class="pagetitle">Chapter 2.4: Composition &amp; Motion</h1>
<p>Composition is choosing <em>what</em> will go <em>where</em> in the frame. We have guidelines for relatively 'standard' framing &mdash; deviating from this norm can have interesting (and sometimes disconcerting) effects on a video. We recommend experimenting.</p>

<p>Smooth motion is sometimes difficult to achieve, but there are tools and tricks that can help you out.</p>

