<h2>Framing Shots with the Rule of Thirds</h2>

<div class="details">
<p>Placing the focal point of your image in the dead center can be disconcerting. Imagine a tic-tac-toe pattern drawn over a frame, use one of the four points where the lines intersect to place the focal point(s) of the shot.</p> 
<p>The focal point of a human subject is generally the head or eyes, depending on how the camera is zoomed, and is often placed along the top horizontal imaginary line. Experiment with the rule of thirds, but <em>don't be a slave to it</em> (or any other, 'rule')</p>
</div> <!-- END DETAILS -->
<img class="left illus" src="images/illustrations/rule_of_thirds_incorrect.png" alt="ROT wrong" style="margin:-10px 20px 10px 10px;" />
<img class="left illus" src="images/illustrations/rule_of_thirds_correct.png" alt="ROT correct" style="margin:-10px 0 10px 0;" />
<h2>Zooming</h2>
<div class="detailz clearfix">
<p>Because internet video is often viewed in a small window, it is good to stay tight on your subject. If your subject is frequently gesturing or otherwise using body language to convey her point, you will want to zoom out in order to catch this additional communication.<img class="right illus" src="images/illustrations/gesturing_wildly.png" alt="Zooms" style="margin:10px 0 0 5px;" /></p>
<p>Avoid making your viewers motion sick with excessive zooming and/or panning. Finally, we recommend you turn off your camera's digital-zoom feature (check the manual), as it can result in excessively pixelated zooms.</p>
</div> <!-- END DETAILS -->

<h2>Tilting and Panning</h2>
<div class="details">
<p>Both of these techniques involve fixing the camera on its axis (often with a tripod). <em>Tilting</em> is angling the lens up or down, while <em>panning</em> is sweeping the lens left or right.</p>
<p>Tilting or panning with a photography tripod (one <em>without</em> a fluid head) can result in jittery movement &mdash; to avoid jitters, you can try finding a consumer grade video tripod. Either way, it takes a good deal of practice to get smooth and accurate tilts and pans.</p>
</div> <!-- END DETAILS -->

<h2>Dollying</h2>
<div class="detailz clearfix">
<p>Dollying is physically moving the camera, while it is fixed to an object. Professionals use dolly tracks<img class="right illus" src="images/illustrations/skateboard_dolly.png" alt="Skateboard Dolly Style" style="margin:10px 0 5px 5px;" /> and/or rigs to smoothly move the camera &mdash; these setups can cost tens of thousands of dollars.</p> 
<p>We recommend wheelchairs, cars, skateboards, trikes, or other improvised wheeled devices (IWDs). Have the camera person sit on the vehicle or object with someone else pushing.</p>
</div> <!-- END DETAILS -->

<h2>Moving with a Steadycam</h2>
<div class="detailz clearfix">
<p>If you're walking or running while shooting, you might consider <a class="wikilink" href="http://www.cs.cmu.edu/~johnny/steadycam/"> building a steadycam</a>. Professional steadycams cost thousands of dollars, but there are some great recipes for homebrew steadycams that do a pretty good job of stabilizing your footage.</p>
</div> <!-- END DETAILS -->

