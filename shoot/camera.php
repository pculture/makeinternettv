<h1>Camera Basics</h1>
<h2>Holding: Single Hand</h2>
<p>One basic technique is holding your camera with one hand and supporting that elbow with your free hand. Keep your elbow near your body, as this will give you the most leverage and allow you to hold the camera for long periods.</p>
</div> <!-- END DETAILS -->
<div class="clear"></div>

<div class="detailz clearfix">
<img class="left illus" src="images/illustrations/holding_twohands.png" alt="Two Hands" /> 
<h2>Holding: Two Hands</h2> 
<p>Sometimes it is more comfortable to hold a camera with both hands. Again, keep your elbows near your body for improved leverage and stability. The difference between one and two hands is largely a matter of personal preference.</p>
</div> <!-- END DETAILS -->
<div class="clear"></div>

<div class="detailz clearfix">
<img class="right illus" src="images/illustrations/holding_abovehead.png" alt="Above Head" style="margin:-30px 0 12px 10px" /> 
<h2>Holding: Above the Head</h2> 
<p>If you're shooting events with crowds, you might need to hold your camera over your head. If you have a swivel viewfinder, this is no problem. However, if you're using a camera without an adjustable viewfinder, it takes a lot of practice to hit your mark.</p>
</div> <!-- END DETAILS -->

<h2>Holding: Video Blog Style Interviewing</h2>
<div class="detailz clearfix">
<p>If you're using a small camera, you can hold it, facing inward at arms length, and capture yourself, plus your interview subject. The technique is tricky and definitely requires practice.<img class="left illus" src="images/illustrations/blog_interview.png" alt="Video Blog Style" style="margin:10px 0;" /> </p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->
<div class="clear"></div>

<div class="callout important">
<h2>Keep Recording</h2>
<p>It's smart to record a little longer than you think you need, as you will have more wiggle room when you're editing.</p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Stabilizing your Camera</h2>
<div class="details clearfix"><img class="right lined" src="images/equipment/shoot_stable.jpg" alt="Shoot Stable" style="margin:0 0 15px 10px;" />
<p>Keep an eye out for architectural or natural features that can help you stabilize your shots. Walls are good for leaning against, and knee-to-elbow action can be helpful -- try to use gravity to your advantage. If you're able to use a tripod, it'll make your shots 98% more stable.</p>
</div> <!-- END DETAILS -->

