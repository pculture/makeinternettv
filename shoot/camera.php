<h1 class="pagetitle">Chapter 2.1: Camera Basics</h1>

<p>These are the basics for holding and operating your camera. Don't be shy about shooting mundane events, interviewing friends, and constantly having your camera ready to go &mdash; practice is good.</p>

<p>The more you use your camera in mundane situations, the easier it is to capture something important on the first try.</p>

