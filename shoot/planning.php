<h2>The Big Picture</h2>

<div class="details">
<p>It is generally wise to have an idea of what sort of footage you want to end up with, and a rough idea of how it will fit together. This is especially true if you plan on working with actors or a film crew, since your ideas might not be as readily understandable to other people as they are to you.</p>
<p>Visualizing your video through the eyes of your audience can be another useful exercise. Just imagine a person sitting at their computer, browsing to your web blog or video site and then watching your video all the way through.</p>
</div> <!-- END DETAILS -->

<h2>Storyboarding, Screenplays, Scribbling and Drawing</h2>
<div class="detailz">
<p><img class="right illus" src="images/illustrations/think_plan.png" style="margin:10px 0 0 5px;" />Many find screenplays and storyboards are a bit overkill for their project. That being said, it can be helpful to draw a shot before you shoot it, especially if someone else is shooting the camera. Screenplay formats are standardized because they make certain directions – on camera and off – much easier to interpret.</p> <p>It might be helpful to first draw the surroundings of your shot first and <strong>then</strong> create a box to indicate the camera's frame position. This allows you to 'see' the composition of a shot before you take the time to record it. </p><p>For beginners, writing a screenplay can be somewhat intimidating but with practice it will become second nature. It might be helpful to find the screenplay for your favorite film online and read along with it in order to get the general idea, some commercial DVDs in fact include this as a special feature. Don't worry about the crazy formatting either, as there are a handful of software options that will handle it for you. <a class="wikilink" href="http://www.celtx.com/">Celtx</a> is a popular, free, multi-lingual and multi-platform solution.   </p>
</div> <!-- END DETAILS -->

<h2>Checklists and "Being Prepared"</h2>
<div class="detailz clearfix">
<p>These are especially important when you're shooting at a remote location. Showing up to an interview with all your microphones, but no audio cables, can totally screw your afternoon.</p> 
<p>A few broad categories that might make good checklist material: <strong>Charged Batteries</strong>, <strong>Camera Accessories</strong>, <strong>Lighting and Accessories</strong>, <strong>Cables and Power</strong>, <strong>Audio and Accessories</strong>, and <strong>Media</strong> (tapes or memory cards).</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Over-Planning</h2>
<p>While being prepared is good, you don't want to spend all of your time planning and none of your time shooting video. Get set and then <strong>go shoot video</strong>.</p>

</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Research and Interviews</h2>
<div class="details">
<p>In order to have pertinent questions, when interviewing an expert in a particular field (be it etymology or Nascar racing), you should take the time to do some homework.</p>
<p>Having both a specific and general knowledge of the subject, is smart. Think of an interview as a conversation &mdash; keep your cool.</p>
</div> <!-- END DETAILS -->

