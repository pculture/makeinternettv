<h1 class="pagetitle">Chapter 2.5: Planning a Shoot</h1>
<p>Sometimes it is good to just grab your camera and shoot indiscriminately, but usually a little planning can save you a <em>lot</em> of time and energy. Planning is especially important when you only have one opportunity to capture an event or interview, or if you've organized a shoot with multiple actors.</p>

<p>This short chapter covers a few things you might want to consider, before starting a production.</p>

