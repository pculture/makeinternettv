<h1 class="pagetitle">Chapter 2.3: Lighting Scenes</h1>
<p>Lighting, often more-so than camera quality, determines whether a video looks amateurish or professional. Using light effectively, whether natural or artificial, takes skill and practice.</p>

<p>Becoming good at lighting takes a lot of practice and familiarity with your equipment. You can use your viewfinder as a rough benchmark for your lighting, but it'll probably look different when you put it on your computer.</p>

