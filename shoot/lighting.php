<h2>A Few Basics of Lighting</h2>

<div class="detailz">
<p>Generally, you will want to keep the strongest lights behind your camera. Use reflectors to redirect light and diminish harsh shadows.<img class="right illus" src="images/illustrations/holding_reflector.png" alt="Holding Reflector" style="margin:5px 0 5px 10px;" /> Silk, or other translucent cloth, can soften and diffuse strong light sources. Try bouncing strong light off walls or ceilings. Continually experiment and practice, and you will eventually begin to 'feel' good vs bad lighting.</p>
</div> <!-- END DETAILS -->

<h2>Natural vs Artificial</h2>
<div class="detailz">
<p>It is generally easiest to rely entirely on natural <em>or</em> artificial lighting, because when the two mix, the colors of the lights clash (the same can be said about mixing fluorescent and halogen lighting). Mixing is possible, but to avoid clashing, you must balance the temperature of the lights. For details, see <em>color temperature</em> (below).</p>
</div> <!-- END DETAILS -->
<div class="clear"></div>

<h2>Using Natural Lighting</h2>
<div class="details">
<p>Shooting in full noon-day sun is not recommended as it casts harsh shadows on a person's face. You're better off shooting in full shade and optionally bouncing extra light into the scene with reflectors. Similarly, shooting in partial shade with intense sunlight will create a very high contrast scene. Shooting out of the shade can be great in the morning or evening, just remember that your lighting will be in flux and eventually too dark or too light. Overcast days are great for getting even lighting.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="ajaxcallout">

<h3><a class="icon_box" href="javascript:Effect.Combo('callbox', {duration: .3});" title="Color Temperature">Color Temperature and White Balancing</a></h3>
<p>Different types of light cast different color temperatures. Our eyes do a great job automatically balancing these colors; however, video cameras are not so clever... <strong>(<a href="javascript:Effect.Combo('callbox', {duration: .3});" title="Views">Click to expand</a>)</strong></p>

<div id="callbox" style="display: none;">
<h2>Color Temperature</h2>
<p class="below-header">We won't go into a lot of detail, but do know that the temperature of incandescent (household light bulb) light, florescent light, light at sunrise, overcast daylight, and clear daylight are all different.</p> 
<p>Color temperature is measured in degrees on the Kelvin scale. Low temperature creates a reddish cast, medium is whitish, and high temperature is blueish. This is why mixing florescent and incandescent lighting can create odd discoloration in your video.</p>
<p>For technical details, see the Wikipedia article <a class="wikilink" href="http://en.wikipedia.org/wiki/Color_temperature">Color Temperature</a>.</p>

<h2>White Balancing</h2>
<p class="below-header">In order to create accurate looking colors, a camera must decide what 'white' should actually look like. Many cameras have pre-selected settings for various types of light. Some cameras even have a manually adjustable white balance. Most professional editing suites will allow you to do some white balancing, after the fact.</p>
</div> <!-- END CALLBOX -->

</div> <!-- END AJAXCALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Three Point Lighting</h2>
<div class="details">
<p>This is the classic formula for lighting a scene, and has been used for many years in both film and photography. Three point lighting generally consists of a key light, a fill light and a back light.</p> 
<p>We present this as a suggestion, but <em>not</em> as a rule -- it is important to be flexible, and trust your eyes. Furthermore, don't feel limited to using only three lights (try one, two or five).</p>
</div> <!-- END DETAILS -->

<h3>Key Light</h3>
<div class="detailz clearfix">
<p><img class="right lined" src="images/shoot/key.jpg" alt="Key Light" />This light is pointed directly at the subject, and sets the stage for the rest of the lighting. However, it generally casts noticeable shadows, and may need to be balanced out by the other lights.<img class="left lined" src="images/shoot/onepoint.png" style="margin:5px 0 5px 0;" /></p>
</div> <!-- END DETAILS -->
<br /><br />
<h3>Fill Light</h3>
<div class="detailz clearfix">
<p><img class="right lined" src="images/shoot/key_fill.jpg" alt="Key and Fill Lights" />This light is generally used to illuminate the background, and wash away any overly harsh shadows that the key light produces. It is generally diffuse and can be very bright. Both fill and back light are often provided by reflectors.<img class="left lined" src="images/shoot/twopoint.png" style="margin:5px 0 5px 0;" /></p>
</div> <!-- END DETAILS -->
<br /><br />
<h3>Back Light</h3>
<div class="detailz clearfix">
<p><img class="right lined" src="images/shoot/key_fill_back.jpg" alt="Key Fill and Back Lights" />This adds definition to your subject, by shining from the back. Depending on the intensity of this light, the effect can be subtle or dramatic.<img class="left lined" src="images/shoot/threepoint.png" style="margin:5px 0 5px 0;" /></p>
</div> <!-- END DETAILS -->
