<h1 class="pagetitle">Chapter 2.2: Capturing Sound</h1>
<p>Sound quality is sometimes considered less important than video, but many experienced videographers would disagree. Lousy sound can spoil an otherwise great production.</p>

<p><em>Always</em> get your microphone as close as you possibly can to the sound source you want to capture. If your microphone is internal or attached to your camera, move everything up close!</p>

