<h2>Isolating Your Sound</h2>
<div class="details">
<p>It is important to decide which sounds you want to capture; focus on isolating those sounds as best you can. When possible, control your environment. For instance, if you're doing an interview in an apartment, switch off all fans, turn of noisy computers, and close any nearby windows. Also consider moving your subject away from unwanted noise sources. If you have no control over your subject or environment, you'll want to position your microphone as close to your subject as possible.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout expert clearfix">
<img class="illus right" src="images/illustrations/broom_boom.png" alt="broom boom" />
<h2>The Boom Broom</h2>
<p>This is a simple trick for getting a microphone closer to the action. Be sure to remind the boom-person to keep their arms bent at the elbows, to avoid passing out.</p>
<p>Make sure the pole and microphone are well out of the frame. Some viewfinders don't display the edge of the shot &mdash; it's wise to add some extra distance.</p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Directional vs Omni-Directional</h2>
<div class="details">
<p>There are three basic microphone categories, based on the directional pattern of sound they pick up: omnidirectional, cardioid (directional), and shotgun (hyper-directional). An omnidirectional microphone picks up sound from every direction, and is good for capturing the atmosphere of a situation. A directional-type microphone will pick up sound from a concentrated direction, you will want to be aware of where this type of mic is pointing.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important clearfix">
<img class="illus right" src="images/illustrations/headphones_monitor.png" alt="Monitor Audio" />
<h2>Always Monitor Your Audio</h2>
<p>When using an external microphone, it's wise to use headphones to make certain everything is hooked up right. If there is a problem with the microphone, you will know about it <em>before</em> you are ready to edit your footage.</p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Using a Directional Microphone</h2>
<div class="details">
<p>When using a directional microphone, be aware of what is behind your subject. When using a shotgun on a boom pole, point from high to low or vice versa, in order to avoid capturing background noise.</p>
</div> <!-- END DETAILS -->

<h2>Recording Externally</h2>
<div class="detailz clearfix">
<p><img class="right lined" src="images/equipment/mp3-recorder.jpg" />If you have no audio input on your camera, you might opt to use an external recorder (like an mp3 or mini-disk player). If you're using an external audio recorder, as well as a camera, you'll want to get both recorders rolling and then give a sharp clap. This will make syncing the external sound to the video <em>far</em> easier. Keep in mind that this is labor intensive and can add a lot of time to your editing process.</p>
</div> <!-- END DETAILS -->

