<h1>Internal or External Microphone?</h1>

<div class="details">
<p>Most digital cameras and some low-end DV camcorders don't have a microphone jack (looks the same as an iPod headphone jack), so you won't need to worry about an external microphone. However, if you <em>do</em> have a microphone input, you will get <em>far</em> better sound if you use an external microphone. Even a $30 Radio Shack microphone will get better sound than an internal microphone.</p>
</div> <!-- END DETAILS -->

<h1>External Microphones</h1>

<h2>Handheld Microphone</h2>
<div class="details">
<img class="right lined" src="images/equipment/mic.jpg" alt="Microphone" />
<p>This is the basic reporter style microphone, you can get one for pretty cheap at Radio Shack. They can be passed back and forth, dangled over a shot, or taped to the end of a pole and kept close to the action.</p>
</div> <!-- END DETAILS -->

<div class="detailz clearfix">
<h2>Lavaliere Microphone</h2>
<img class="left lined" src="images/equipment/lav.jpg" alt="Lav Microphone" style="margin-top:10px;" />
<p>These are clipped on to a subject's lapel or shirt, and are great when you need to capture audio from just one person.</p>
</div> <!-- END DETAILS -->

<div class="clear"> </div>


<h2>Shotgun Microphone</h2>
<div class="details">
<img class="right lined" src="images/equipment/shotgun.jpg" alt="Shotgun Microphone" style="margin:-10px 0 5px 20px;" />
<p>A decent shotgun mic is generally more expensive than a comparable lavaliere or handheld mic, and most "real" shotguns are XLR, so you might need an adapter (see below). The pickup pattern is very directional; thus, what you point at is what you hear, so they are very nice for capturing a person's voice who is speaking to a crowd.</p> 
<p>Some cameras have a standard size hotshoe mount that will accomodate a shotgun, but many are proprietary &mdash; test carefully, before you purchase a shotgun and mount.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="clear"> </div>

<div class="callout expert clearfix">
	<div class="twopic">
<img class="right lined" src="images/equipment/xlr.jpg"  alt="XLR plug" />
<img class="right lined" src="images/equipment/mini_jack.jpg" alt="Stereo Mini-Jack" />
	</div>
<h2>XLR and Stereo Miniplug</h2>
<p style="margin-right:130px;">Professional grade microphones generally have XLR connectors, but most DV camcorders don't have XLR inputs. However, it is possible to use XLR microphones if you have the right adapter.</p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<div class="clear"> </div>

<h2>Wireless System</h2>
<div class="details">
<p>A reliable wireless system isn't usually cheap. We don't recommend relying on consumer grade wireless for an important shoot, as it is very susceptible to interference and can cause more problems than it solves. If you use wireless, make sure you monitor the audio with headphones.</p>
</div> <!-- END DETAILS -->

<div class="detailz clearfix">
<h2>Headphones</h2>
<img class="right lined" src="images/equipment/headphones.jpg" alt="Headphones" style="margin:15px 0 10px 5px;" />
<p>If you're using an external microphone <strong>monitor your audio!</strong> Just plug headphones into your camera's audio-out jack and keep an ear on the audio.</p> 
<p>Headphones enable you to catch poor connections, line interference (electronic or radio), and environmental interference (background noise), before you begin editing the footage.</p> 
<p>We recommend earbuds, plugs, or monitor style headphones.</p>
</div> <!-- END DETAILS -->

