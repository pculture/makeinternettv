<h1 class="pagetitle">Chapter 1.2: Audio Equipment</h1>
<p>Beginners sometimes jump to the conclusion that sound quality is less important than image quality, but many experienced videographers will say the opposite is true. Lousy sound can completely spoil an otherwise great production.</p>

<p><em>Always</em> get your microphone as close as you possibly can to the sound source you want to capture. If your microphone is internal or attached to your camera, move everything up close!</p>
