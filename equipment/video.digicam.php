<h1 class="pagetitle">Chapter 1.1a: Choosing a Digital Camera</h1>
<p>Digital cameras make great internet video recording devices: inexpensive, light, small, and convinient. You can also find hybrid digital cameras that have some of the features of a traditional camcorder (higher image quality, manual controls, etc), but offer the convinience of recording directly to a memory card.</p>

<p>When buying online, or using review sites, such as <a class="wikilink" href="http://www.dpreview.com/">Digital Photography Review</a>, we suggest you check out a few cameras in person. This will give you a better idea of what to expect from various brands and form factors.</p>

