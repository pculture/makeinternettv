<h1>Purchasing a Digital Camera:</h1>

<div class="detailz clearfix">

<h2>Image Quality</h2>
<div class="details">
<img class="right lined" src="images/equipment/digicam_2.jpg" alt="Digital Camera" style="margin-top:-25px;" />
<p>The best way to judge image quality is to search the web for footage shot from specific models of digital cameras. Of course the video may be compressed or otherwise not of the highest quality, but it's a starting point. Otherwise, you'll need to rely on the resolution, frames per second, and mega-pixel count for a <em>very rough</em> estimate of picture quality.</p>
</div> <!-- END DETAILS -->

<h2>Video Resolution</h2>
<p>Resolution is the number of dots, aka pixels, a camera can record. More dots means more details. Many digital cameras shoot standard definition (640x480 pixels), but some shoot in HD (720x1080 pixels). Older cameras may record 320x240 pixels.</p>
</div> <!-- END DETAILS -->

<h2>Sound Quality</h2>
<div class="details">
<p>Don't count on high quality sound from a digital camera. Just get as close to your sound source as you can. <p>It is also possible to record audio with an external recorder and then sync the image and sound with editing software (clap when you start recording and then match the audio tracks). Beware that this adds a <em>lot</em> of time to your project and probably isn't practical in most cases.</p>
</div> <!-- END DETAILS -->


<h2>Storage Space and Format</h2>
<div class="details">
<p><img class="right lined" src="images/equipment/memory.jpg" alt="Memory Cards" style="margin-top:-20px;" />Most cameras come with a low-capacity memory card; you'll probably want to upgrade. Just check and make sure you get the proper format.</p>
<p>The amount of shooting time you can record to a given card varies from camera model to model, and also depends on resolution and compression settings on the camera.</p>
</div> <!-- END DETAILS -->

<h2>Battery Life</h2>
<div class="details">
<p>Independent digital camera review sites are often a good source for finding realistic battery life estimates for digital cameras (manufacturers often report optimistically high operation time). If you plan on recording outdoors for extended periods, you might consider a spare battery.</p>
</div> <!-- END DETAILS -->

<h2>Proprietary Batteries</h2>
<div class="detailz clearfix">
<p><img class="right lined" src="images/equipment/batteries.jpg" alt="batteries" />Most cameras use proprietary rechargable batteries, while others operate on standard AA or AAA batteries. Proprietary batteries generally provide longer life, but they may require a recharging station and cost more to replace.</p>
</div> <!-- END DETAILS -->

<h2>Frames Per Second (fps)</h2>
<div class="details">
<p>Frames per second is exactly what it sounds like -- the number of video frames captured in a second. Most digital cameras capture a standard 30fps, but some older models only do 15fps.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Short Recording Time</h2>
<p> Certain older digital cameras can only record video for a short time (30-45 seconds) before they must stop and write the footage to memory. Others can record until the memory card fills up. Check for these limits <em>before</em> you want to capture something important.</p> 
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Ergonomics and User Interface</h2>
<div class="details">
<p>Even if you buy online, we recommend checking out a few brands of digital cameras in stores. Hold a variety of models and brands to see which shape is most comfortable. Try putting it in your pocket or bag (to check the bulk, <em>not</em> to hijack it).</p>
</div> <!-- END DETAILS -->

