<h2>Don't Forget the Tripod</h2>
<p>Tripods are great for doing fixed shots, they can make video shot on a less expensive device appear more professional. If you want to move the camera (panning side to side or tilting up and down), you'll want a tripod with a <em>fluid head</em> &mdash; this will keep your movements from jittering. You can find consumer grade fluid-head tripods for under $100. Regular tripods can usually be found for cheap at garage sales or thrift stores.</p>
