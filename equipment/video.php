<h3><a class="icon_box" href="javascript:Effect.Combo('callbox', {duration: .3});" title="Fair Use" name="fairuse">Cellphones, Hard Disks, Hybrids, and HD?</a></h3>
<p>Nowdays, many devices blur the lines we've drawn between the camera and camcorder. This mini-section can help you get sorted. <strong>(<a href="javascript:Effect.Combo('callbox', {duration: .3});" title="Views">Click to expand</a>)</strong></p>

<div id="callbox" style="display: none;">
<h2>What are the Differences?</h2>
<p>When we say <strong>DV Camcorder</strong>, we're meaning a camcorder that records to tape and has a digital output. The <strong>Digital Camera</strong> we're talking about is of the point and shoot variety. It records to a memory card and shares more characteristics with hybrids, cell phones, and other newer styles video devices.</p>  

<h2>High-Definition (HD)</h2>
<p>HD means a higher volume of video information is recorded, resulting in a better image. This feature is becoming increasingly common on consumer camcorders and hybrids, and will soon be standard on everything from point and shoot digital cameras to cell phones.</p>

<h2>Hybrid Video Cameras</h2>
<p>Hybrids help span the gap between digital camera and camcorder &mdash; they always record direct to a memory card, so no digitization is reqired. The quality of the lens, microphone, and hardware can vary greatly, but these are usually better for video than a similarly priced point and shoot digital camera. Higher end hybrids can rival quality (and price) of a more traditional camcorder.</p>

<h2>Hard Drive Camcorders</h2>
<p>Hard drive camcorders record video to a tiny hard disk drive, instead of a memory card. They are not recommended, because small spinning hard drives are prone to break. This technology may eventually mature, but for now you should steer clear.</p>

<h2>Cell Phone Video Cameras</h2>
<p>Picture quality typically diminishes as devices become smaller simply because their size makes it difficult to pack in decent recording equipment. Battery life and media length may be of concern, depending on the device as well.</p>
<p>Make sure your sales associate or smart friend teaches you how to record and upload video to your computer (or the internet) &mdash; these instructions vary greatly between devices. That said, cell phone video can be recorded extremely discreetly, and may be useful in documenting social issues or emergencies.</p>
