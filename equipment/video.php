<h3>Should you use a Digital Camera?</h3>
<p>The digital camera can be small, agile, and is generally easier to keep handy at all times. They can often be found for little money, and are non-intrusive and great for doing informal interviews. Also, it's pretty easy publish digital camera footage <em>without editing it</em>.</p>
<p>The downside is in the quality department. The lenses are usually cheaper and the cameras often don't have external micropone jacks. That said, they're simple, inexpensive, and great beginner cameras. I know professionals who sometimes choose to use a digital-type camera for ease of use.
</div> <!-- END DETAILS -->
