<h3>Do you need a DV Camcorder?</h3>
<p>DV camcorders generally give a much higher level of control over both picture and sound quality than digital cameras (especially at similar prices). A DV camcorder is often physically larger, and requires footage to be digitized (unless it's a hybrid that records to memory card), and accessories add to the bulk (tapes, microphones, larger batteries, etc.).</p>
<p>Despite these disadvantages, if you need the improved image and sound quality, the tradeoff is well worth it. Camcorders are great when you want higher production levels &mdash; just make sure you know how to properly light your shots before you make a huge investment.</p>
