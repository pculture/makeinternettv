<h1>Make Your Own Lighting Kit</h1>

<h2>Reflectors</h2> 
<div class="details">
<img class="right lined" src="images/equipment/reflectors.jpg" alt="Reflectors" style="margin:-10px 0 10px 5px;" />
<p>Reflectors are a basic tool, and can be useful in a variety of situations. Foamcore from the hardware store, auto sun shades (foil or matte, for a different effect), or even white cardboard will all work for reflecting light. Keep one or two of these handy.</p>
</div> <!-- END DETAILS -->

<div class="clear"> </div>
 
<div class="details">
<img class="left lined" src="images/equipment/clamp-lamp.jpg" alt="Clamp Lamps" />
<h2>Clamp Lamps</h2>
<p>Clamp lamps are a good low cost light source that can be purchased on the cheap at almost any hardware store. Bulb wattage can be varied to control the intensity.</p>
</div> <!-- END DETAILS -->

<div class="clear">&nbsp;</div>

<div class="details">
<img class="right lined" src="images/equipment/work-lamp.jpg" alt="Work Lamps" style="margin:-18px 0 20px 5px;" />
<h2>Work Lamps</h2>
<p>These are a heavier duty light source than the clamp lamp. They are relatively inexpensive, available at most hardware stores, and some even come with tripods.</p>
</div> <!-- END DETAILS -->

<div class="details">
<img class="left lined" src="images/equipment/chinaball.jpg" alt="Chinese Lanterns" />
<h2>Chinese Lanterns</h2>


<p>Also known as chinaballs, these cast an evenly diffused soft light. Get them in chinatown, at many box stores (ikea, target, etc), in an import store (possibly expensive), online, and even in some professional video/photography shops. Use extreme caution when ligthing with a Chinese lantern -- they are flammable! Usually you need to get the light socket and bulb separately (100-200 watts).</p>
</div> <!-- END DETAILS -->

<h2>Light Diffusion</h2>
<div class="details">
<p>You can tone down a harsh light by diffusing it with a piece of fabric. You'll need to find some white silk or translucent nylon (ripstop works well). After that, you must figure out a way to safely place the diffusion panel between your light source and your subject.</p>
</div> <!-- END DETAILS -->

<h2>More DIY Lighting</h2>
<div class="details">
<p>The internet has a ton of awesome resources for Do It Yourself lighting. You can find tricks, short-cuts, cool equipment, and other low cost techniques to make your video look professional.</p>
</div> <!-- END DETAILS -->

<div class="clear">&nbsp;</div>

</div> <!-- END INTERIOR -->

<div class="callout important">
<img class="lined co" src="images/equipment/surge_protector.jpg" alt="Cords" style="margin-bottom:5px;" /> &nbsp;
<h2>Cords and Surge Strips</h2>
<p>Extension cords will come in handy. Also, when you're plugging in your lights, use a surge protector -- this will keep you from blowing a fuse or breaker.</p>
</div> <!-- END CALLOUT -->

