<h2>Collaboration and Vendor Lock-In</h2>
<p>When you're working with others on video projects, everything is easier if everyone is using the same editing platform/program. You can always save in the same format, and be able to easily access each others' work.</p> 
<p>This is known as <a class="wikilink" href="http://en.wikipedia.org/wiki/Vendor_lock-in">Vendor Lock-in</a>, and is one of the plagues of digital video editing. Keep this in mind before you collaborate on a project.</p> 
