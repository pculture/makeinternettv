<h2>USB and Firewire Ports</h2>
<p>If you're using a DV camcorder, you'll need a USB 2.0 port <em>or</em> Firewire port (also known as "i.Link" or "IEEE 1394"). Make sure you match the camera port to the camera. Some older computers only have USB 1.0 ports, which are not fast enough to transfer video data from a DV camcorder. There are a variety of port sizes and shapes &mdash; just do your research ahead.</p>
<p>Digital cameras and hybrid cameras usually just require some type of USB port (1.0 or 2.0 will both work).</p>
</div> <!-- END CALLOUT -->

