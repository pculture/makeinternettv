<h1 class="pagetitle">Chapter 1.1b: Choosing a DV Camcorder</h1>
<p>DV Camcorders are the more expensive, complex, bulkier route. However, if you're serious about higher production values and the potential for a crisper final product, this might be your game.</p>

<p>Even when buying online or searching review sites, such as <a class="wikilink" href="http://reviews.cnet.com/camcorders/">CNET Camcorder Reviews</a>, we suggest you check out a few camcorders in person. This will give you an idea of what to expect from various brands and form factors.</p>

