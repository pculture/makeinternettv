<h1>Purchasing a DV Camcorder:</h1>

<div class="detailz clearfix">
<h2>Standard or High-Definition</h2>
<img class="right illus" src="images/equipment/dv_cam_2.jpg" alt="DV Camcorder" style="margin-top:-25px;" />
<p>High-Definition (HD) video is crisper and more detailed than standard definition. Most video hosting websites now support HD, and the price of an HD camera has dropped considerably. If you want the potential to create more professional grade video, HD may be the way to go.</a>
</div> <!-- END DETAILS -->

<h2>Recording Medium</h2>
<div class="details">
<p>If you're getting a DV Camcorder and want to edit your footage, we recommend models that use <strong>mini-DV tape</strong>, as opposed to hard disk or DVD (which aren't always as reliable). Certain hybrid models record to memory card, and may be a happy medium between camcorder and digital camera.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Computer Ports!</h2>
<p>You'll need a USB 2.0 port <em>or</em> Firewire port (also known as "i.Link" or "IEEE 1394"). Some older computers only have USB 1.0 ports, which are not fast enough to transfer video data. Make sure your camera and computer have matching ports.</p>

</div> <!-- END CALLOUT -->

<div class="interior">

<h2>Battery Life</h2>
<div class="details">
<p><img class="right illus" src="images/equipment/batteries_dv.jpg" alt="batteries" style="margin-top:-10px;" />Camcorder review sites can be a good source for finding realistic battery life (manufacturers often report optimistically high operation time). If you plan on recording outdoors for extended periods, you might consider a spare or higher capacity battery.</p>
</div> <!-- END DETAILS -->

<h2>Image and Sound Quality</h2>
<div class="details">
<p>You can find examples of image quality on camcorder review sites, some sites even review the internal microphone quality.</p> 
<p>If you really want great sound, you'll need to use an external microphone. Just make sure you have an <strong>audio input jack</strong> if you plan to use an external microphone.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="ajaxcallout">

<h3><a class="icon_box" href="javascript:Effect.Combo('callbox', {duration: .3});" title="Advanced Features">Advanced Features</a></h3>
<p>If you're looking to get superior sound and video, here are two advanced features to look for... <strong>(<a href="javascript:Effect.Combo('callbox', {duration: .3});" title="Views">Click to expand</a>)</strong></p>

<div id="callbox" style="display: none;">
<h2>Automatic Gain Control</h2>
<p class="below-header">For better audio, find a camcorder with the option to <em>turn off</em> Automatic Gain Control (ACG).</p> 
<p>ACG automatically changes the sensitivity of the microphone, such that in a low noise situation it will become responsive and pick up every little noise. In some cases this might be good, but often it is best to have manual control over the microphone.</p>
<h2>White Balance Controls</h2>
<p class="below-header">For more control over the picture, look for a camera with white balance settings.</p> 
<p>Pre-determined settings are okay, but manual white balance gives you the ability to make white actually look white (and make skin tones look natural). If you're using a professional editing suite, you can white balance during editing.</p>
</div> <!-- END CALLBOX -->

</div> <!-- END AJAXCALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>Ergonomics and User Interface</h2>
<div class="details">
<p>Even if you buy online, we recommend checking out a few brands of DV camcorders in stores. Hold a variety of models and brands to see which shape is most comfortable. Look into the viewfinder and make sure you're satisfied with your ability to hold the picture steady. If you travel a lot, make sure it is a comfortable size.</p>
</div> <!-- END DETAILS -->

