<div class="steps">
<h2><span class="number">1</span> Export (with expert settings)</h2>
<p>Use <strong>File > Export (Shift + Apple + E)</strong> to bring up the export dialog. Select the <strong>QuickTime</strong> icon, from the top menu.</p>

<img class="steps" src="images/edit/mac/export-01.jpg" alt="Export Settings" />
<p>Make sure you set it to Compress movie for: <strong>Expert Settings</strong>.</p>

<img class="steps" src="images/edit/mac/export-02.jpg" alt="Expert Settings" />
<p>Now press <strong>Share</strong>.
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Save exported file as...</h2>
<p>Once you press <strong>Share</strong>, the "Save As" dialogue will pop up.</p>
<img class="steps" src="images/edit/mac/export-03.jpg" alt="Save As" />
<p>Now set Export to <strong>Movie to MPEG-4</strong> and then click <strong>Options.</strong></p>
<img class="steps" src="images/edit/mac/export-04.jpg" alt="MPEG-4" />
</div> <!-- END STEP 2 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Compression and Archiving</h2>
<p>Much like digital photography, once a photo is compressed and the original file is destroyed, it is impossible to reclaim lost resolution and clarity. You can archive an original, but watch out &mdash; video can eat copious amounts of drive space. For details, see <a href="#">Archiving for Mac</a>.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">3</span> Pick Your Settings</h2>
<p>Once you press <strong>Options</strong>, a new window will pop up. Set the File Format to: <strong>MP4</strong>.</p>
<img class="steps" src="images/edit/mac/export-05.jpg"alt="MP4 Export Settings" />
<p>Highlight the <strong>Video</strong> tab, and use these following settings:</p>
<img class="steps" src="images/edit/mac/export-06.jpg" alt="Video Tab" />
<p>Next, highlight the <strong>Audio</strong> tab, and use these settings:</p>
<img class="steps" src="images/edit/mac/export-07.jpg" alt="Audio Tab" />
<p>Finally, highlight the <strong>Streaming</strong> tab, and use these settings:</p>
<img class="steps" src="images/edit/mac/export-08.jpg" alt="Streaming Tab" />
<p>Now just press <strong>OK</strong>...</p>
<img class="steps" src="images/edit/mac/export-09.jpg" alt="OK" />
<p>And then press <strong>Save</strong>.</p>
<img class="steps" src="images/edit/mac/export-10.jpg" alt="Save" />
<p>The amount of time it takes to compress video depends on the speed of your computer, the length of your video, and your settings.</p>
<img class="steps" src="images/edit/mac/export-11.jpg" alt="Compressing" />
</div> <!-- END STEP 3 -->

