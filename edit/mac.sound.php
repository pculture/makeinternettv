<div class="steps">
<h2><span class="number">1</span> Pick a Spot</h2>
<p>Move your playhead to the spot that you want the music or sound effect to begin.</p>
<img class="steps" src="images/edit/mac/sound-01.jpg" alt="Place Your Playhead" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Import Your Audio</h2>
<p>Use <strong>File > Import (Apple + Shift + I)</strong>, and browse to the directory containing mp3's, sound effects and/or other audio. Select the audio file and click <strong>open</strong>.</p>
<img class="steps" src="images/edit/mac/sound-02.jpg" alt="Import a File" />
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> You've Got Music</h2>
<p>The audio appears on one of the two lower bars of the timeline. You can easily drag it onto either the top or bottom level, and even have two songs playing simultaneously.</p>
<img class="steps" src="images/edit/mac/sound-03.jpg" alt="You've Got Music" />
</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Volume Control</h2>
<p><strong>Click</strong> on an audio (or video) clip and then <strong>drag</strong> the volume control to adjust the volume level.</p>
<img class="steps" src="images/edit/mac/sound-04.jpg" alt="Volume Control" />
</div> <!-- END STEP 4 -->

<div class="steps">
<h2><span class="number">5</span> Fading and Controlling Music</h2>
<p>To take full control over any audio (or video) clip's sound, <strong>Control + Click</strong> on a clip and choose the option: <strong>Show Clip Volume Levels</strong>.</p>
<img class="steps" src="images/edit/mac/sound-05.jpg" alt="Full Control" />

<p>The purple line mirrors the sound level. Click on it to create drag points that can be stretched and moved up or down.</p>

<img class="steps" src="images/edit/mac/sound-06.jpg" alt="Control Points" />

</div> <!-- END STEP 5 -->

