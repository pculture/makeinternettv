<h2>Apple</h2>
<div class="detailz clearfix">
<p>New Apple computers running OSX include a free copy of iMovie. iMovie is an incredibly easy to use, basic video editing program. iMovie has a, arguably, more intuitive interface than Windows Movie Maker.</p>
