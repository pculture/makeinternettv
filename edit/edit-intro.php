<h2>Windows (PC)</h2>
<div class="detailz clearfix">
<p>All versions of Windows XP and Vista have free access to a basic video editing program, called Windows Movie Maker (WMM). If you already have access to a Windows XP computer, we recommend trying WMM, before you look into more advanced (potentially expensive) alternatives.</p>
