<h2>GNU/Linux</h2>
<div class="detailz clearfix">
<p>GNU/Linux is a totally kick ass operating system. However, it is decidedly immature when it comes to video editing; this is due, in part, to proprietary video codec schemes. That said, if you are a GNU/Linux user, it <em>is</em> possible to edit video, using solely free and open source software.</p>
