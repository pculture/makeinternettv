<div class="steps">
<h2><span class="number">1</span> Import Audio File(s)</h2>
<p>Choose <strong>File > Import into Collections (Ctrl + I)</strong> and browse to your audio files.</p>
<img class="steps" src="images/edit/win/sound-01.jpg" alt="Place Your Playhead" />
<p>Select one or more audio file and click <strong>Import</strong>.
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Add the Audio File</h2>
<p>First, make sure the <strong>Collections</strong> sidebar is showing <strong>(View > Collections)</strong>, and then choose the <strong>Collections</strong> folder.</p>
<img class="steps" src="images/edit/win/sound-02.jpg" alt="Collections" />
<p><strong>Single click</strong> the audio file and choose <strong>Clip > Add to Timeline (Ctrl + D)</strong> or <strong>drag and drop</strong> the file from your Collections onto the timeline.</p>
</div> <!-- END STEP 2 -->

</div> <!-- END INTERIOR -->

<div class="callout important clearfix">
<h2>Reveal the Entire Timeline</h2>
<img class="co" src="images/edit/win/sound-tip-01.jpg" alt="Reveal Timeline" />
<p>If you can't see the audio you imported, <strong>click</strong> and <strong>drag</strong> the top of the timeline up to reveal the sound/music tracks.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">3</span> Arrange, Cut and Splice</h2>
<p>The audio should appear on the <strong>Audio/Music</strong> portion of the timeline.</p>
<img class="steps" src="images/edit/win/sound-03.jpg" alt="You've Got Music" />
<p>Use all the same techniques you used to <a href="edit/win.arrange.php">arrange video</a> clips, in order to move and cut audio tracks.</p>
</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout important clearfix">
<h2>MP3 Files Crash Windows Movie Maker?</h2>
<p>It seems that standard mp3 files sometimes crash WMM &mdash; if you experience this inconvenience, check out <a class="wikilink" target="_blank" href="http://mitvwiki.org/Converting_Audio_Files_with_Windows">Converting Audio Files with Windows</a></p>
</div> <!-- END CALLOUT -->

<div class="interior">


<div class="steps">
<h2><span class="number">4</span> Volume Control</h2>
<p><strong>Right Click</strong> on an audio clip and then click <strong>volume</strong> to adjust the volume level on the entire clip.</p>
<img class="steps" src="images/edit/win/sound-04.jpg" alt="Volume Control" />
<p>Additionally, you can add a <strong>Fade</strong> to the beginning or end of the clip.</p>
</div> <!-- END STEP 4 -->


