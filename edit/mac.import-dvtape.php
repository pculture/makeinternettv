<div class="steps">
<h2><span class="number">1</span> Plug in Your DV Camcorder</h2>
<p><strong>Plug your DV camcorder into your computer</strong> via Firewire or USB 2.0.</p>
<img class="steps" src="images/edit/mac/dvcam_plugin.jpg" alt="Plug in DV Camcorder" />
<p><strong>Power up</strong> and set the camcorder to VTR or VCR mode.</p>
</div> <!-- END STEP 1 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<img class="co" src="images/edit/mac/mac-tip-02.jpg" alt="Menu Commands" />
<h2>Menus and Commands</h2>
<p>Whenever you see a command, such as <strong>File > Save Project</strong>, it refers to commands in the menu bar (top of the screen).</p>
<p>Keyboard shortcuts are given in parenthesis; for example, <strong>(Apple + S)</strong> indicates that the apple key should be held and the 'S' key should be pressed once.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">2</span> Open iMovie, Create New Project</h2>
<p><strong>Open iMove</strong> and create a new project using <strong>File > New (Apple + N)</strong>.</p>
<img class="steps" src="images/edit/mac/import-create.jpg" alt="Create a New Project" />
<p>Name the project, choose a destination and then press <strong>Create</strong>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Queue and Record Your Footage</h2>
<p>Queue up the footage you want to capture, on your DV camcorder. You can usually use the <strong>fast forward, rewind, play, stop</strong> and <strong>import</strong> buttons in iMovie to control your camcorder (otherwise, queue the footage with the controls on the camcorder).</p>
<p>Once the footage you want to record is queued up, press <strong>import</strong> to capture it. Once you're done capturing, press <strong>stop</strong>. Repeat as necessary.</p>
<img class="steps" src="images/edit/mac/import-connected.jpg" alt="Camera Connected" />
</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Videos Appear in the Clip Library</h2>
<p>Now the footage should appear in the clips library. Just click on the name of a clip to rename it.</p>
<img class="steps" src="images/edit/mac/import-clips.jpg" alt="Import a File" />
</div> <!-- END STEP 4 -->


