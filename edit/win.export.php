<div class="steps">
<h2><span class="number">1</span> Save a Movie File</h2>
<p>Once your video is finalized, use <strong>File > Save Movie File (Ctrl + P)</strong>.</p>
<img class="steps" src="images/edit/win/export-01.jpg" alt="Select Device" />
<p>Select <strong>My computer</strong>, and then click <strong>Next</strong>.</p>
<img class="steps" src="images/edit/win/export-02.jpg" alt="Select Device" />
<p>Choose a name and a destination, and then click <strong>Next</strong>.</p>
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Choose Video Export Quality</h2>
<img class="steps" src="images/edit/win/export-03.jpg" alt="Select Device" />
<p>First, click the <strong>Show more choices</strong> link.</p>
<img class="steps" src="images/edit/win/export-04.jpg" alt="Select Device" />
<p>For sharing on the internet, we recommend exporting as <strong>Video for broadband (512 Kbps)</strong>.</p>
<p>For a reasonably high-quality copy that will play back nicely on your computer, choose <strong>Best quality for playback on my computer</strong>.
<p>If you want a full-quality version, you can choose the <strong>DV-AVI</strong> setting (the file may take a lot of storage space).</p>
<img class="steps" src="images/edit/win/export-05.jpg" alt="Select Device" />
<p>Depending on the speed of your computer and length of your video, this process could take a while. Once it's finished, your finalized movie file will be saved to wherever you specified in step 1.</p>
</div> <!-- END STEP 2 -->

