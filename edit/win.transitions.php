<h1>Transitions and Effects</h1>

<div class="details">
<p>An <strong>effect</strong> works on a single clip, sometimes only affecting the beginning or end of the clip. <strong>Transitions</strong> require two clips to be next to each other.</p>
</div> <!-- END DETAILS -->

<h2>Using Effects</h2>

<div class="details">
<p>There are a lot of effects in the library, but we'll stick with fading in and out, which is a simple example.</p>
</div> <!-- END DETAILS -->

<div class="steps">
<h2><span class="number">1</span> Fading a Clip In/Out With an Effect</h2>
<img class="steps" src="images/edit/win/transitions-01.jpg" alt="Select a Clip" />
<p>Select a clip with a mouse click.</p>
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> The Effects Library</h2>
<p>First, make sure the <strong>Collections</strong> sidebar is showing <strong>(View > Collections)</strong>, and then choose the <strong>Video Effects</strong> folder.</p>
<img class="steps" src="images/edit/win/transitions-02.jpg" alt="Effects Library" />
<p><strong>Single click</strong> one of the <strong>Fade In</strong> or <strong>Fade Out</strong> effects.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Add Your Effect</h2>
<p>Choose <strong>Clip > Add to Timeline (Ctrl + D)</strong> to add the effect. Alternatively, you can <strong>Drag and Drop</strong> the effect onto the clip.</p>
<img class="steps" src="images/edit/win/transitions-03.jpg"alt="Add Effect" />
<p>The blue star indicates that an effect has been applied &mdash; you can add more than one effect to a clip.</p>
</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout important clearfix">
<h2>A Quick Way to Fade In/Out</h2>
<img class="co" src="images/edit/win/transitions-tip-01.jpg" alt="Fade In/Out" />
<p>If you <strong>right click</strong> on the video portion of a clip, you can select <strong>Fade In</strong> or <strong>Fade Out</strong> (to black).</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<h2>Using Transitions</h2>

<div class="details">
<p>There are a lot of transitions in the library, but we'll stick with a simple fade between two clips.</p>
</div> <!-- END DETAILS -->

<div class="steps">
<h2><span class="number">1</span> Add a Fade Transition Between Two Clips</h2>
<p>Drag the playhead to a spot between two clips.</p>
<img class="steps" src="images/edit/win/transitions-04.jpg" alt="Transition Set" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> The Effects Library</h2>
<p>First, make sure the <strong>Collections</strong> sidebar is showing <strong>(View > Collections)</strong>, and then choose the <strong>Video Transitions</strong> folder.</p>
<img class="steps" src="images/edit/win/transitions-05.jpg" alt="Transitions Library" />
<p><strong>Single click</strong> one of the <strong>Fade In</strong> or <strong>Fade Out</strong> effects.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Add Your Transition</h2>
<p>Choose <strong>Clip > Add to Timeline (Ctrl + D)</strong> to add the transition. Alternatively, you can <strong>Drag and Drop</strong> the transition between the clips.</p>
<img class="steps" src="images/edit/win/transitions-06.jpg"alt="Add Transition" />
<p>The small grey strip indicates that a transition has been applied to the clips.</p>
</div> <!-- END STEP 3 -->

