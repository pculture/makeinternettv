<div class="steps">
<h2><span class="number">1</span> Choose a Destination</h2>
<p>Position your playhead where you would like the title to begin.</p>

<img class="steps" src="images/edit/mac/titles-01.jpg" alt="Place Playhead" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> The Titles Library</h2>
<p>Select the <strong>Editing</strong> menu, and then choose the <strong>Titles</strong> sub-menu.</p>
<img class="steps" src="images/edit/mac/titles-02.jpg" alt="Titles Library" />
<p>There are many to choose from &mdash; we're going to demonstrate a basic <strong>Centered Title</strong>.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Create a Centered Title</h2>
<p>Select the <strong>Centered Title</strong> option.</p>
<img class="steps" src="images/edit/mac/titles-03.jpg"alt="Centered Title" />
<p>Fill your title/subtitle into the box. You can also customize the color (white is the default).</p>
<img class="steps" src="images/edit/mac/titles-04.jpg" alt="Fill in Your Title" />
<p>Choose a font, typeface, and size. If you'd like a second title, use the plus <strong>"+"</strong> icon (below the color picker).</p>
<img class="steps" src="images/edit/mac/titles-05.jpg" alt="Size and Font" />
</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Pace Your Title</h2>
<p>The <strong>Speed Slider</strong> dictates how long it takes for your title to fade in and out. The <strong>Pause Slider</strong> is the amount of time your title stays, between fading in/out. The <strong>Duration</strong> is simply the total sum of the <strong>Speed</strong> and <strong>Pause</strong>.</p>
<img class="steps" src="images/edit/mac/titles-06.jpg" alt="Pace Your Title" />
<p>Check out the description of <strong>Timecode</strong> (below) to understand what the <strong>Speed</strong> and <strong>Pause</strong> durations actually mean.</p>
</div> <!-- END STEP 4 -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Timecode and FPS</h2>
<img class="co" src="images/edit/mac/transitions-tip-01.png" alt="Timecode" />
<p>In the US and Japan, standard video has 29.97 frames per second (FPS), while almost everyone else in the world has 24 FPS. In the US, 00:00:15 is close to one half second.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">5</span> Preview Your Title</h2>
<p>Once you have your variables filled chosen, single click the highlighted title (<strong>Centered Title</strong>, in this case). Click in the preview window to change the placement of your title.</p>
<img class="steps" src="images/edit/mac/titles-07.jpg" alt="Preview Your Title" />
<p>Any time you change or update your title, just single click the type of title and you'll get a preview.</p>
</div> <!-- END STEP 5 -->

<div class="steps">
<h2><span class="number">6</span> Put Your Title into Your Video</h2>
<p>To place your title, simply click <strong>Add</strong>.</p>
<img class="steps" src="images/edit/mac/titles-08.jpg" alt="Add Your Title" />
<p>Alternatively, you can drag and drop the title (<strong>Centered Title</strong>, in this case) onto the timeline.</p>
<img class="steps" src="images/edit/mac/titles-09.jpg" alt="Add Your Title" />
</div> <!-- END STEP 6 -->

