<h1 class="pagetitle">Chapter 3.3g: Creating Titles</h1>
<p>Titles can go almost anywhere in a video. Sometimes they are used for credit or introduction, while other times they are used to narrate or instruct.</p>
<p>Getting creative with titles, in iMovie, is sort of difficult, due to lack of full control. We recommend you figure out a simple way to do titles and stick with it.</p>
