<h1>Where to Begin?</h1>

<h2>Kino + Ubuntu</h2>
<div class="details">
<p>This seems like a very promising solution for GNU/Linux video editing. <a class="wikilink" href="http://www.ubuntu.com/">Ubuntu</a> is an easy-to-use GNU/Linux distribution. <a class="wikilink" href="http://www.kinodv.org/">Kino</a> is the GNU/Linux equivalent to iMovie and Windows Movie Maker.</p>
</div> <!-- END DESCRIPTION -->

<h2>Kdenlive + Ubuntu</h2>
<div class="details">
<p>There are also reports that <a class="wikilink" href="http://www.kdenlive.org/">Kdenlive</a> is a good solution when you're using footage shot on a digital camera.</p>
</div> <!-- END DESCRIPTION -->

</div>  <!-- END INTERIOR -->

<div class="callout important">
<h2>Save Early, Save Often</h2>
<p>Almost anyone with experience editing video on computers has lost work due to a crash. <strong>Save your work early</strong> and <strong>continually save at regular intervals</strong>. Trust us here.</p>
</div> 

<div class="interior">

<h1>Advanced Options</h1>

<h2>Alternative Editing Programs</h2>
<div class="details">
<p><a class="wikilink" href="http://cvs.cinelerra.org">Cinelerra</a> is a more advanced free and open source editing suite (more similar to AVID, Final Cut, or Premiere). <a class="wikilink" href="http://www.pitivi.org/wiki/Main_Page">PiTivi</a> is another open source editing suites for GNU/Linux.</p>
</div> <!-- END DESCRIPTION -->

