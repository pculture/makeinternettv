<div class="steps">
<h2><span class="number">1</span> Plug in Your Camera or Memory Card</h2>
<p>If the footage is on your digital camera, you will need to plug it in and power it up.</p>
<img class="steps" src="images/edit/win/digicam_plugin.jpg" alt="Plug in Camera" />
<p> Most cameras show up as a disk drive (like a USB memory stick).</p>
</div> <!-- END STEP 1 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<img class="co" src="images/edit/win/win-tip-02.jpg" alt="Menu Commands" />
<h2>Menus and Commands</h2>
<p>Whenever you see a command, such as <strong>File > Save Project</strong>, it refers to commands in the menu bar (top of the screen).</p>
<p>Keyboard shortcuts are given in parenthesis; for example, <strong>(Ctrl + S)</strong> indicates that the control key should be held and the 'S' key should be pressed once.</p>
</div> <!-- END CALLOUT -->

<div class="interior">


<div class="steps">
<h2><span class="number">2</span> Open WMM, Import into Collections</h2>
<p>Open Windows Media Maker and choose <strong>File > Import into Collections (Ctrl + I)</strong>.</p>
<img class="steps" src="images/edit/win/import.jpg" alt="Import from Camera" />
<p><strong>Browse to your camera's video folder</strong>, select the videos you want, and click <strong>import</strong>.</p>
</div> <!-- END STEP 2 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Trouble Importing or Finding the Video on Your Camera?</h2>

<p>If you cannot use or find the videos, they probably aren't in a Windows compatible format (the files might end in .mov or .mp4).</p> 
<p>You can try converting them to the Windows Media format with the free video transcoding software <a class="wikilink" href="http://sourceforge.net/projects/mediacoder">MediaCoder</a></p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">3</span> Videos Appear in My Collections</h2>
<p>Now the footage should appear in the clips library.</p>
<img class="steps" src="images/edit/win/import-collections.jpg" alt="Files are in Collections" />
<p><strong>Single click</strong> on a clip and press <strong>F2</strong> to rename it.</p>
</div> <!-- END STEP 3 -->

