<div class="steps">
<h2><span class="number">1</span> Arrange Your Clips</h2>
<p>Drag clip from your clip library onto the workspace timeline.</p>
<img class="steps" src="images/edit/mac/arrange-01.jpg" alt="Clip Library" />
<img class="steps" src="images/edit/mac/arrange-02.jpg" alt="Timeline" />
</div> <!-- END STEP 1 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<img class="co" src="images/edit/mac/arrange-tip-01.jpg" width="76" height="109" alt="Timeline vs. Storyboard" />
<h2>Timeline View vs. Storyboard View</h2>
<p>Try both views: <strong>toggle the icon shown to the right</strong> (find it above the timeline).</p> 
<p>Viewing the project as a storyboard may seem more intuitive, but <strong>the timeline view shows the relative length</strong> of video and audio clips.</p>
<p><strong>We'll be using the timeline view for this tutorial.</strong></p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">2</span> Cut and Splice</h2>
<p>Position the playhead where you want to split a clip, and then use <br /><strong>Edit > Split (Apple + T)</strong> to cut the clip into two. Drag pieces back up to your clip library or send them to the trash with the delete key.</p>
<img class="steps" src="images/edit/mac/arrange-03.jpg" alt="Position Playhead" />

<p>Next, press <strong>Edit > Split (Apple + T)</strong></p>
<img class="steps" src="images/edit/mac/arrange-04.jpg"alt="and Cut" />
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Shave the Edges Off</h2>
<p>Highlight (single click) any clip and <strong>set the crop markers</strong> (triangles).</p>
<img class="steps" src="images/edit/mac/arrange-05.jpg" alt="Set Crop Marks" />
<img class="steps" src="images/edit/mac/arrange-06.jpg" alt="Close Crop Marks" />

<p>Only the stuff in yellow stays &mdash; all excess footage is deleted.</p>

<img class="steps" src="images/edit/mac/arrange-07.jpg" alt="Crop It" />

<p>When you're ready to crop, press <strong>Edit > Crop (Apple + K)</strong>.</p>

</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<img class="co" src="images/edit/mac/arrange-tip-02.jpg" width="175" height="110" alt="Precision Zooming" />
<h2>Zooming and Precision</h2>
<p>Get a different perspective on the timeline with the zoom slider. When on the timeline, the <strong>left and right keys</strong> will nudge your playhead a single frame.</p>
</div> <!-- END CALLOUT -->

