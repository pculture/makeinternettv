<h1>Uploading Your Video to YouTube</h1>

<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Create a YouTube account <a class="wikilink" href="http://youtube.com/signup">here</a>.</p>
<img class="steps" src="images/publish/service/youtube-01.gif" alt="Create an Account" />
<p>You will need to check your e-mail for a confirmation link and all the usual registration stuff.</p>
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Name and Tag Your Video</h2>
<img class="steps" src="images/publish/service/youtube-02.gif" alt="Upload Tab" />
<p>Log in, and click the <strong>Upload Videos Link</strong>.</p>

<img class="steps" src="images/publish/service/youtube-03.jpg" alt="Upload" />
<p>Next, name your video, give it a concise description and tag it with keywords (for search). You will also need to pick a category.</p>

<img class="steps" src="images/publish/service/youtube-04.jpg"alt="Browse" />
<p>Now, click <strong>Continue Uploading</strong> to move forward.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Find Your File</h2>

<img class="steps" src="images/publish/service/youtube-05.jpg"alt="Browse" />
<p>Now click <strong>Browse</strong>...</p>

<img class="steps" src="images/publish/service/youtube-06.jpg"alt="Open the File" />
<p>And find your video file.</p>

</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Upload It!</h2>

<img class="steps" src="images/publish/service/youtube-07.jpg" alt="Upload It" />
<p>Hit <strong>Upload Video</strong>.</p>
<p>Be very patient and don't browse away from this page. This step can easily take 15+ minutes.</p>

<img class="steps" src="images/publish/service/youtube-08.jpg"alt="Wait" />
<p>Now, you're done.</p>

</div> <!-- END STEP 4 -->

<h1>What's Next?</h1>

<p>Now that you've got video(s) uploaded, you need people to watch them.</p>
