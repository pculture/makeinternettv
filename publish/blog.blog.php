<h1>Get a Blog</h1>

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Do It Yourself (DIY) Blog vs Hosted Blogging Service</h2>
<p>If you want the most control over your video blog, you'll need a DIY blog hosted on your on web space. This allows a custom URL, like <em>NewsVidsbyGeorge.com</em>. Wordpress offers both a hosted and DIY option for their software.</p>

<p>Blogsolid's advice on <a class="wikilink" href="http://blogsolid.com/ideas/2007/start-a-wordpress-blog-on-your-own-site-part-1-finding-the-right-host/">DIY Wordpress Installation</a>  <br />and CNET's <a class="wikilink" href="http://reviews.cnet.com/web-hosting-buying-guide/">DIY Web Hosting Buying Guide</a></p>

<p>If you go the DIY route (and pick Wordpress as your platform), we strongly recommend the plugins available on the <a class="wikilink" href="http://showinabox.tv/">Show in a Box</a>.</p>

</div> <!-- END CALLOUT -->

<div class="interior">
<p>Below are instructions for creating blogging account at Wordpress.com. In case you're interested, here's a list of alternative <a class="wikilink" href="http://en.wikipedia.org/wiki/Blog_hosting_services#Developer-hosted">hosted blogging services</a>.</p>
<div class="steps">
<h2><span class="number">1</span> Get a Wordpress Account</h2>
<br />
<p>Create a Wordpress.com blog <a class="wikilink" href="http://wordpress.com/signup/">here</a>.</p>

<img class="steps" src="images/publish/blog/blog-01.jpg" alt="Create an Account" />
<p>Fill out the forms and make sure you check <strong>Gimmie a Blog!</strong>.</p>
<img class="steps" src="images/publish/blog/blog-02.jpg" alt="Next!" />
<p>Now click the <strong>Next</strong> button.</p>
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Choose a Domain Name</h2>
<img class="steps" src="images/publish/blog/blog-03.jpg" alt="Domain" />
<p>On the second page, you'll be picking a <strong>Blog Domain</strong>. This is important, because it's the web address for your blog.</p>

<img class="steps" src="images/publish/blog/blog-04.jpg" alt="Signup" />
<p>Fill in the remaining info and click <strong>Signup</strong>.</p>

<p>You will need to check your e-mail for a confirmation link and all the usual registration stuff.</p>

</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Log In to Your New Blog</h2>
<br />
<p>Once you've gotten an e-mail with a password, browse to the domain you chose.</p>
<img class="steps" src="images/publish/blog/blog-05.jpg" alt="Browse to Account" />

<img class="steps" src="images/publish/blog/blog-06.jpg" alt="Find Meta" />
<p>Find the <strong>Meta</strong> section in the sidebar and then click the <strong>Login</strong> link.</p>

<img class="steps" src="images/publish/blog/blog-07.jpg" alt="Login" />
<p>Use your login credentials; you should have gotten them by e-mail after having followed the confirmation link.</p>

<img class="steps" src="images/publish/blog/blog-08.jpg" alt="Logged In" />
<p>If all went well, you're logged in to your new blog (what once said 'login' should now say 'logout').</p>

<p>Now you've got a fully functional blog! Now let's get the second element of your videoblog going.</p>
</div> <!-- END STEP 3 -->

