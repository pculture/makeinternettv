	<h2>Before you Begin!</h2>
	<p>TubeMogul allows you to upload your videos to several sharing sites simultaneously <i>but in order to take advantage of this you will need an account for each site you choose to upload too </i>. </p><p>In step 3 below there is an opportunity to do so with direct links to each registration page, however it makes more sense to spend a little time visiting each website you intend to upload to and get a feel for what kind of content they typically feature or promote. </p>	
	</div> 
	<!-- END CALLOUT -->
	
	<div class="clear"> </div>

<div class="interior">
<h1>Working with TubeMogul</h1>	

<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<img class="steps" src="images/publish/service/tm-01.gif" alt="Upload Tab" />

<p>Create your TubeMongul account <a class="wikilink" href="http://www.tubemogul.com/register.php"> here</a>.</p>

<p>You will need to check your e-mail for a confirmation link and all the usual registration stuff.</p>
</div> 
<!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Upload to TubeMogul</h2>

<p>Once you've logged in, you'll see the <strong>Dashboard</strong>. </p>

<img class="steps" src="images/publish/service/tm-02.gif" alt="Upload a Video" />

<p>Simply click on <strong>Upload a Video</strong>.</p>

<p>Click <strong>Browse</strong> to find your video and <strong>Upload</strong> to send it to TubeMogul's sever.</p>

<img class="steps" src="images/publish/service/tm-04.gif" alt="Browse then Upload" />


<p>From there you can add a title, description, tags, category, and choose a thumbnail (when applicable) for each website you choose to use. Be careful! Choosing the right words here can make a big difference when someone tries to search for your video.</p>

<img class="steps" src="images/publish/service/tm-03.gif" alt="Add descriptions" />

<p>Once your video is done uploading to TubeMogul's servers, click <strong> Upload </strong> again at the bottom of the page.</p>

<img class="steps" src="images/publish/service/tm-05.gif" alt="Click Upload Once More" />


</div> 
<!-- END STEP 2 -->

</div>
<!-- END INTERIOR -->

<div class="clear"> </div>

<div class="callout important">
<h2>A Note about File Size </h2>
<p>Depending on the length and quality (as in file size, not its chance to win an award) of the video you choose to upload, TubeMogul may re-encode or compress your video in order to work within the various specifications of each individual sharing site. <strong>If you're not sure what that means, don't worry about it too much right now</strong>. This may be an issue worth exploring more in the future, but the settings we chose in chapter 3 mean it probably won't be an issue. Either way, TubeMogul will handle the conversion, but you may want to consider dividing your videos into smaller chunks to avoid this problem.</p>	
</div> 
<!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<div class="steps">
<h2><span class="number">3</span> Deploy!</h2>

<p>The next page you're brought to is where TubeMongul starts to get fun. Enter log-in information for the various sites you wish to upload your video to. Click "more options" to go to the website's registration page. Make sure you click the checkbox for each one as you make your way down the list.</p> 

<img class="steps" src="images/publish/service/tm-06.gif"alt="Upload" />
<p>Once you've added the necessary info, make sure you've marked the check box next to each site you want to upload to and click <strong>Launch</strong> down below.</p>

<img class="steps" src="images/publish/service/tm-07.gif"alt="Upload" />

<p>TubeMogul will automatically upload and submit each video to the service of your choosing. The process isn't instantaneous, but it would take <strong>much longer</strong> to do this for each individuals site. As an added bonus, you can navigate away from the page or even close your browser if you'd like. You'll know they have been successfully added to each sharing service you have selected when the yellow icon turns to green.

<img class="steps" src="images/publish/service/tm-08.gif"alt="Upload Complete" />

</div> 
<!-- END STEP 3 -->

