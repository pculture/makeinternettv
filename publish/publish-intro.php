<h1>Pick One of the Following:</h1>

<div class="details">

</div> <!-- END DETAILS -->

<h2>A Video Service (easy to use, less control)</h2>

<div class="details">
<p><strong>The upside:</strong> Quick and easy to upload videos. Easy discoverability &mdash; users browsing the site may find your videos.</p> 
<p><strong>The downside:</strong> The presentation, format, and compatibility (with things like iPods) is often left up to the video service. For example, YouTube automatically surrounds your video with distractions like ads or other related videos. Also, if you're ever dissatisfied with your video hosting service, picking up and moving your videos and audience can be difficult. However, there are hybrid approaches that can minimize this issue. </p>
</div> <!-- END DETAILS -->

<div class="choose">
	<a href="./publish/service.php"><h4>Choose: &nbsp;<em>Video Service</em></h4></a>
</div>	
<div class="clear"></div>

</div> <!-- END INTERIOR -->
<br />
<div class="callout important">
<h2>Hybrid Publishing</h2>

<p>You can get many of the benefits of a video sharing service and a video blog by using <em>both together</em>. This means you upload videos to a video service and then add each new video to a blog post.</p> 

<p>Also, you can always begin with a video sharing site and add a blog to your arsenal later.</p>
<p></p>
</div> <!-- END CALLOUT -->

<div class="interior">

<h2>Video Blog (more set-up, you get more control)</h2>

<div class="details">
<p><strong>The upside:</strong> This unlinks the video hosting from your audience, which means that if you don't like the hosting, you can switch it without losing any viewers. You also get more control of video presentation, quality, and resolution. Finally, it has the benefits of being a blog (comments, pings, customization, blogrolls, etc.).</p> 
<p><strong>The downside:</strong> Setting up a blog is more work than starting an account with a video sharing service. You need a video host, and either a blog or a blogging service. Putting videos into a video blog, depending on your setup, can be more laborious than posting to a service.</p>
</div> <!-- END DETAILS -->
