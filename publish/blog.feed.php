<h1 class="pagetitle">Chapter 5.3b: Your RSS Feed</h1>
<p>RSS feeds are the lifeblood of the blogosphere. They're what allow people to do podcasting, use newsreaders, and keep track of their favorite bloggers without needing to visit tens or hundreds of sites all the time (because all the latest media comes directly to their newsreader or podcast client).</p>

<p>Most blogs default to Text RSS, which is slightly different than a feed for podcasting or video podcasting. In order to get the RSS feed to work as a video podcast, you can use a free service called Feedburner. This is a simple one-time task.</p>
