<h1>Get a Video RSS Feed</h1>

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Attention DIY bloggers and non-Wordpress.com folks</h2>
<p>If you use a blogging service other than Wordpress.com, step 1 will be a bit different for you. Your goal is to <strong>find your blog's RSS feed URL</strong>. Once you have it, move to step 2.</p>
<p>For help, see <a href="./promote/subscribe.knowyourfeed.php#blogfeedfinder">Finding Your Blog's Feed</a>.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">1</span> Find Your Wordpress RSS Feed Address</h2>
<br />
<p>Browse to your Wordpress blog.</p>

<img class="steps" src="images/publish/blog/feed-01.jpg" alt="Go to Your Blog" />
<p>An RSS feed is a URL, just like any other link on a website.</p>

<img class="steps" src="images/publish/blog/feed-02.jpg" alt="Feed URL" />
<p>Scroll to the bottom of the page (sometimes it's in the sidebar's "meta" section) and look for the RSS feed links.</p>

<img class="steps" src="images/publish/blog/feed-03.jpg" alt="Copy URL" />
<p><strong>Right click</strong> (for mac, <strong>ctrl + click</strong>) on <strong>Entries (RSS)</strong>. Now select <strong>Copy Link Location</strong> (<strong>Copy Shortcut</strong> for Internet Explorer).</p>

</div> <!-- END STEP 1 -->

<div id="burnafeed" class="steps">
<h2><span class="number">2</span> Burn a Video RSS Feed</h2>
<br />
<p>Browse to <a target="_blank" href="http://feedburner.com/">Feedburner.com</a>.

<img class="steps" src="images/publish/blog/feed-04.jpg" alt="Feedburner" />

<img class="steps" src="images/publish/blog/feed-05.jpg" alt="Burn URL" />
<p>Use <strong>Edit > Paste</strong> to input your RSS address into the field. MAKE SURE YOU CHECK THE BOX THAT SAYS <strong>I am a podcaster!</strong></p>

<p>Now click the <strong>Next</strong> button.</p>

</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Create a Feedburner Account</h2>
<br />
<p>Now Feedburner will prompt you to create an account.</p>
<img class="steps" src="images/publish/blog/feed-06.jpg" alt="Create an Account" />
<p>Make sure your feed title and address are good, and then fill out the account information. Once you're set, press the <strong>Activate Feed</strong> button.</p>

<img class="steps" src="images/publish/blog/feed-07.jpg" alt="Burnt URL" />
<p>The link in blue is your <strong>video RSS feed URL</strong>. In order to have viewers subscribe to your videos, they will need this magic link. <strong>Write it down</strong>. We'll show you how to use it in Chapter 6: <a href="promote/promote-info.php">Promote</a>.</p>
</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Customize Your Feedburner Feed</h2>
<p>Feedburner has a ton of advanced options, such as custom feed icons, descriptions and titles &mdash; this allows your viewers to get a better experience when they subscribe to your feed. Look on the Feedburner site for more details.</p>
</div> <!-- END CALLOUT -->

