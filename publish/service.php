<h1>What Differentiates Sharing Sites?</h1>

<div class="details">
<p>We'll compare some different video hosting sites in depth here. At the bottom of this page, you'll find a list of even more places to host video.</p>
</div> <!-- END DETAILS -->

<img src="images/publish/service/blip_logo.gif" style="margin-bottom:1em;" alt="blip" />

<div class="details">
<p><strong>The upside:</strong> blip.tv gives the creator more control over their work, which allows for more flexible presentation. They have an opt-in advertising service and share revenue 50/50 with the creator. blip.tv provides a very simple RSS subscription system. Longer form videos can be uploaded in High-Definition and are generally playable immediately after upload.</p>
<p><strong>The downside:</strong> blip.tv does not command the massive YouTube audience; as a result, their library isn't searched nearly as often.</p>
</div> <!-- END DETAILS -->
<div class="choose">
    <a href="./publish/service.blip.php"><h4>Choose: &nbsp;<em>blip.tv</em></h4></a>
</div>
<div class="clear"></div>
<br /><br />

<img src="images/publish/service/youtubelogo.gif" style="margin-bottom:1em;" alt="youtube" />
<div class="details">
<p><strong>The upside:</strong> YouTube is incredibly popular. Their massive user-base is constantly searching and perusing the YouTube library. They have an exceedingly simple interface for publishing video. They support High-Definition video.</p>
<p><strong>The downside:</strong>YouTube gives the video creator little freedom as to how their videos are presented, shared, and licensed. Videos uploaded to YouTube aren't always immediately playable. YouTube does not share ad revenue with just any creator &mdash; only those accepted into their ad sharing program. Normal users have a max video length of 10 minutes.</p>
</div> <!-- END DETAILS -->

<div class="choose">
    <a href="./publish/service.youtube.php"><h4>Choose: &nbsp;<em>YouTube</em></h4></a>
</div>
<div class="clear"></div>
<br /><br />

<img src="images/publish/service/ia.gif" style="margin-bottom:1em;" alt="Archive.org" />

<div class="details">
<p><strong>The upside:</strong> The Internet Archive is a 501(c)(3) non-profit that was founded to build an internet library, with the purpose of offering permanent access for researchers, historians, and scholars. The site strongly encourages Creative Commons licenses, which means that most of the videos on the site can be freely redistributed and/or remixed. </p>
<p><strong>The downside:</strong> The Internet Archive has a terrible browsing interface, and lacks readily-available media feed functionality (media feeds are covered in chapter 6: <a href="./promote/audience.php">Promote</a>). </p>
</div> <!-- END DETAILS -->
<div class="choose">
    <a href="./publish/service.archive.php"><h4>Choose: &nbsp;<em>Archive.org</em></h4></a>
</div>
<div class="clear"></div>
<br />

<img src="images/publish/service/tubemogul_logo.gif" style="margin-bottom:1em;" alt="tubemogul" />
<div class="details">
<p><strong>The upside:</strong> TubeMogul isn't really a site for sharing, but a site that allows you to easily upload to multiple sites in a few short clicks. It's free for personal use and, once configured, can save you serious time. The site also includes tools to promote and track useful stats like viewership across multiple sites.</p>
<p><strong>The downside:</strong> You still have to go through the process of signing up for and checking the quality of your videos on each site, inviting the potential to create more work than you bargained for. They also use a lot of marketing lingo which may be off-putting for some. </p>
</div> <!-- END DETAILS -->

<div class="choose">
    <a href="./publish/service.tubemogul.php"><h4>Choose: &nbsp;<em>TubeMogul</em></h4></a>
</div>
<div class="clear"></div>
<br /><br />

<h1>Alternative Services</h1>

<div class="details">
<p>In our opinion, YouTube, blip.tv and Archive.org are three archetypical examples of video sharing sites: one with a massive user-base, one which gives the author a great deal of control over her or his videos, and one with a non-profit social mission. TubeMogul is a great choice if your objective is to cast as wide of a net as possible, and it includes some useful promotion tools that are covered in chapter 6.  
</p> 
<p>They are great sites, but they are by no means the only option &mdash; we encourage you to look at some alternative services.</p>
</div> <!-- END DETAILS -->
