<h1>Alternative Sharing Sites</h1>

<div class="details">
<p>Below are a few more great video sharing sites. Discuss these (and others) at <a class="wikilink" href="http://MITVWiki.org/Video_Sharing_Services">Video Sharing Services</a>.</p>
</div> <!-- END DETAILS -->



<br style="margin-bottom:.4em;" />
<img class="title border" src="images/publish/service/engage_logo.jpg" alt="engage" />

<div class="details">
<p><a class="wikilink" href="http://engagemedia.org/">EngageMedia</a> is a non-profit with a mission to provide media tools to activists, campaigners, communities and citizen journalists. Any video format you upload will be available to your viewers. The site strongly encourages Creative Commons licenses, which means that many of the videos on the site can be freely redistributed and/or remixed. Sounds like your cup of tea? Check out our <a  class="wikilink" href="./publish/service.engage.php">tutorial</a> to learn more.</p>
</div> <!-- END DETAILS -->

<br style="margin-bottom:.4em;" />
<img class="title border" src="images/publish/service/dailymotion_logo.png" alt="Dailymotion" />

<div class="details">
<p><a class="wikilink" href="http://www.dailymotion.com/">Dailymotion</a> is a very popular video sharing service, based in France. They have a large user-base and are easy to use. Get started with Dailymotion <a  class="wikilink" href="http://www.dailymotion.com/login?urlback=%2Fupload&restricted_action=1">here</a>.</p>
</div> <!-- END DETAILS -->

<br style="margin-bottom:.4em;" />
<img class="title border" src="images/publish/service/revver_logo.png" alt="Revver" />

<div class="details">
<p><a class="wikilink" href="http://one.revver.com/revver">Revver</a> is the first video sharing service that offered revenue sharing with users. Each clip displays a brief advertisement (still, at the time of writing) at the end of the video &mdash; the video publisher gets a portion of the advertising revenue when a viewer clicks the ad. <a class="wikilink" href="http://revver.com/account/register/">Click here</a> to create a Revver account.</p>
</div> <!-- END DETAILS -->

<br style="margin-bottom:.4em;" />
<img class="title border" src="images/publish/service/phpmotion.gif" alt="PHPMotion" />

<div class="details">
<p><a class="wikilink" href="http://phpmotion.com/">PHPMotion</a> is a free server-side video sharing software that also has support for other types of media. Setting it up will take a lot more time than simply registering with the other video service websites mentioned here but the added flexibility, customization potential, and robust admin interface might make it exactly what you need. See it in action <a  class="wikilink" href="http://phpmotion.com/content/view/42/184/">here</a>.</p>
</div> <!-- END DETAILS -->

