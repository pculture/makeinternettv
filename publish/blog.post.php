<h1>Make a Video Blog Post</h1>

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>I Don't Blog with Wordpress.com</h2>
<p>If you use a blogging service other than Wordpress: the process is a bit different, but the principles are the same.</p>
<p><strong>Basic Steps:</strong> Create a new post, add your video URL (a screenshot is optional), and publish the post.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">1</span> Access the Wordpress Admin Interface</h2>
<br />
<p>Browse to your Wordpress blog and <strong>log in</strong> (revisit step 3 from <a target="_blank" href="publish/blog.blog.php">The Blog</a> if you don't remember how to log in).</p>

<img class="steps" src="images/publish/blog/post-01.jpg" alt="Site Admin" />
<p>Click the <strong>Site Admin</strong> link to access your blog's dashboard.</p>

</div> <!-- END STEP 1 -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<img class="co" src="images/publish/blog/post-tip-01.jpg" alt="Dashboard" />
<h2>The Admin Interface</h2>
<p>This is the control panel for your blog. You can write new posts, edit or delete existing posts, and customize your blog.</p>
<p>Share customization and usage tips at <a class="wikilink" target="_blank" href="http://mitvwiki.org/Wordpress.com_Customization">Wordpress.com Customization</a></p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">2</span> Create a New Post</h2>
<br />
<img class="steps" src="images/publish/blog/post-02.jpg" alt="Write" />
<p>Click the <strong>Write</strong> tab and a blank post will appear.</p></p>

<img class="steps" src="images/publish/blog/post-03.jpg" alt="Post Title" />
<p>Choose a descriptive title.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><a name="screenshot"></a><span class="number">3</span> Take a Screenshot</h2>
<br />
<p>A screenshot isn't mandatory, but it's definitely a nice touch. Leave your browser open while you do this step.</p>
<img class="steps" src="images/publish/blog/post-04.jpg" alt="Play Your Video" />
<p>Browse to your video file and double click to play it. Find a compelling frame to use as your screenshot and then press pause.</p>

<p><strong>For Mac:</strong></p>

<p>Hold <strong>apple + shift</strong> and press <strong>4</strong>. Your cursor should turn into a cross. Now press your <strong>spacebar</strong> once. The cursor should turn into a camera. Hover the camera over your video and click your <strong>mouse button</strong> once.</p>
<img class="steps" src="images/publish/blog/post-05.jpg" alt="Mac Screen" />
<p>The screenshot should appear on your desktop as <strong>Picture 1.png</strong>.</p>

<p><strong>For Windows XP:</strong></p>

<p>Unfortunately, Windows XP isn't as easy to get screenshots from... screenshots aren't mandatory, but they're highly recommended.</p>
<p>Share and find resources for <a class="wikilink" target="_blank" href="http://mitvwiki.org/Taking_a_Screenshot_With_Windows_XP">Taking a Screenshot With Windows XP</a></p>

<p><strong>For Windows Vista:</strong></p>
<p>We haven't had a chance to dig into Vista yet!</p>
<p>Share and find resources for <a class="wikilink" target="_blank" href="http://mitvwiki.org/Taking_a_Screenshot_With_Windows_Vista">Taking a Screenshot With Windows Vista</a></p>
</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Upload Your Screenshot</h2>
<br />
<p>Below your post, there is an upload interface.</p>
<img class="steps" src="images/publish/blog/post-07.jpg" alt="Upload Screen" />
<p><strong>Browse</strong> to the screenshot, <strong>open it</strong>, and then click the <strong>Upload</strong> button.</p>

<img class="steps" src="images/publish/blog/post-08.jpg" alt="Full Size" />
<p>Now a small version of your screenshot will appear in the window; scroll down and check the <strong>Full size</strong> button.</p>

<img class="steps" src="images/publish/blog/post-09.jpg" alt="Send to Editor" />

<p>Scroll down further and click <strong>Send to editor</strong>.</p>

</div> <!-- END STEP 4 -->

<div class="steps">
<h2><span class="number">5</span> Link to Your Video</h2>
<br />
<p>The full size screenshot should appear in your post window.</p>
<img class="steps" src="images/publish/blog/post-10.jpg" alt="Link It" />
<p><strong>Single click the image</strong> to highlight it and then click the <strong>link chain</strong> icon on the toolbar. You can also center the screenshot by clicking the <strong>center text</strong> icon on the toolbar.</p>

<img class="steps" src="images/publish/blog/post-11.jpg" alt="Link Address" />
<p><strong>Edit > Paste</strong> the URL of your video file into the <strong>Link URL</strong> field and click <strong>Insert</strong> button.</p>

<p>(revisit step 5 from <a target="_blank" href="publish/blog.video.php">The Video</a> if you don't remember the URL).</p>

<img class="steps" src="images/publish/blog/post-12.jpg" alt="Text Link" />

<p><strong>If don't want a screenshot:</strong> write something like <strong>Video Link</strong>, highlight the text, click the <strong>Link Chain</strong> icon, and paste in your video URL.</p>

</div> <!-- END STEP 5 -->

<div class="steps">
<h2><span class="number">6</span> Publish the Post</h2>
<br />
<p>Click to the right of your screenshot and <strong>press enter once</strong>.</p>
<img class="steps" src="images/publish/blog/post-13.jpg" alt="Describe It" />
<p>Now write a short description of your video. We recommend giving viewers instructions for watching the video (such as, <strong>click screen to watch</strong>).</p>

<img class="steps" src="images/publish/blog/post-14.jpg" alt="Publish" />
<p>Now click the <strong>Publish</strong> button.</p>

<img class="steps" src="images/publish/blog/post-15.jpg" alt="View Site" />
<p>Congrats on making a video blog post! To see it, visit the front page of your blog.</p>
</div> <!-- END STEP 6 -->

