<h1 class="pagetitle">Chapter 5.3b: Hosting Your Video</h1>
<p>The key to this step is getting your video file(s) online. There are a few ways to put your video online: paid web hosting, Internet Archive, or using the hybrid approach we mentioned earlier &mdash; uploading to a video sharing service.</p>
<p>If you pick the hybrid approach (which is a good option), we recommend using a host like <a href="publish/service.blip.php">blip.tv</a> that gives you access to the video file itself, instead of just an embeddable Flash video.</p>
