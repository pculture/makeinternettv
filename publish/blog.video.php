<h1>Upload Your Videos</h1>

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Alternative Option 1: Hybrid Approach</h2>
<p>You can upload videos to a <a href="publish/service.php">video sharing service</a>, and then embed/link the videos into each post. This gives you some of the ease of use of a video service, but also allows you more ownership of your audience and options for customizing your presentation. We highly recommend <a href="publish/service.blip.php">blip.tv</a> for this option.</p>
</div> <!-- END CALLOUT -->

<div class="callout expert">
<h2>Alternative Option 2: Paid Web Hosting</h2>
<p>You can upload videos (usually via FTP) to a paid web host.</p>
<p>For more information, see CNET's <a class="wikilink" href="http://reviews.cnet.com/web-hosting-buying-guide/">Web Hosting Buying Guide</a></p>
</div> <!-- END CALLOUT -->

<div class="interior">
<p>Here, we'll show you how to use the Internet Archive to upload a video to the internet for free.</p>
<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Create an Archive.org account <a class="wikilink" href="http://www.archive.org/account/login.createaccount.php">here</a>.</p>
<img class="steps" src="images/publish/service/ia-01.gif" alt="Create an Account" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Log In and Publish</h2>
<img class="steps" src="images/publish/service/ia-02.gif" alt="Publish" />
<p>Make sure you're <a class="wikilink" href="http://www.archive.org/account/login.php">logged in</a> and select <strong>Publish Video</strong>.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Fill in the Details</h2>

<img class="steps" src="images/publish/service/ia-03.gif"alt="Details" />
<p>Fill in the name of your work and click <strong>next</strong>. </p>

<img class="steps" src="images/publish/service/ia-04.gif"alt="Enter More Info" />

<p>The next page is fairly self explanatory, with a variety of description fields to fill out including licensing information (we think licensing is so important, <a href="license/license-intro.php">we wrote a whole chapter on it</a>). </p>
<img class="steps" src="images/publish/service/ia-05.gif"alt="waiting" />

<p>Once you press <strong>Upload Files</strong>, your video will begin to upload &mdash; this process can take a <strong>LONG</strong> time to complete, depending on your connection and video length. <strong>Just leave the browser window open,</strong> new page will open once this process is complete.</p>

</div> <!-- END STEP 3 -->

<div class="steps">
<h2><span class="number">4</span> Get Your Video URL</h2>
<img class="steps" src="images/publish/blog/video-15.jpg" alt="Media Link" />
<p>You will be redirected to a page with a link, click it.</p>

<img class="steps" src="images/publish/blog/video-16.jpg" alt="Submit Movie" />
<p>If you see the same page (from step 4), wait five to ten minutes and reload.</p>

<img class="steps" src="images/publish/blog/video-17.jpg" alt="Media!" />
<p>Once the video has processed, a new page will load, revealing your media.</p>

<img class="steps" src="images/publish/blog/video-18.jpg" alt="Media Link" />
<p>Now <strong>right click</strong> (on mac, <strong>ctrl + click</strong>) the filetype, in the left side-bar, and select <strong>Copy Link Location</strong> (<strong>Copy Shortcut</strong> for Internet Explorer). This copies the video URL.</p>

<p><strong>Now that you have your video URL, you're ready to make a blog post.</strong></p>
</div> <!-- END STEP 4 -->

