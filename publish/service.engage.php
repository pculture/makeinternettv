<div class="interior">
<h1>Uploading Your Video to Engage Media</h1>

<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Create an EngageMedia account <a target="_blank" href="http://www.engagemedia.org/join_form">here</a>.</p>
<img class="steps" src="images/publish/service/engage-01.jpg" alt="Create an Account" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Log In and Publish</h2>
<img class="steps" src="images/publish/service/engage-02.jpg" alt="Publish" />
<p>Make sure you're <a target="_blank" href="http://www.engagemedia.org/login_form">logged in</a> and select <strong>Publish Video</strong>.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> Fill in the Details</h2>

<img class="steps" src="images/publish/service/engage-03.jpg"alt="Details" />
<p>Fill in the name and description &mdash; a lot of the information is optional, so don't feel overwhelmed. When you have this information set, go to the second section.</p>

<img class="steps" src="images/publish/service/engage-04.jpg"alt="Choose a License" />

<p>EngageMedia encourages a <strong>Creative Commons</strong> license.</p>
<p>We think licensing is so important, <a href="license/license-intro.php">we wrote a whole chapter on it</a>.</p>

<img class="steps" src="images/publish/service/engage-05.jpg"alt="Video and Thumbnail" />
<p>You will need to browse to find your video file, and your optional thumbnail. Plus, you'll need to input the length of the video (if you can't be exact, just guess).</p>

<p>Once you press <strong>Save</strong>, your video will begin to upload &mdash; there is no meter or indicator, and this process can take a <strong>LONG</strong> time to complete, depending on your connection and video length. <strong>Just leave the browser window open.</strong>

</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
