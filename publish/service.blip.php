<div class="interior">
<h1>Uploading Your Video to blip.tv</h1>

<div class="steps">
<h2><span class="number">1</span> Create an Account</h2>
<br />
<p>Create a blip.tv account <a class="wikilink" href="http://blip.tv/users/create/">here</a>.</p>
<img class="steps" src="images/publish/service/blip-01.gif" alt="Create an Account" />
</div> <!-- END STEP 1 -->

<div class="steps">
<h2><span class="number">2</span> Locate Your Video</h2>
<img class="steps" src="images/publish/service/blip-02.jpg" alt="Upload Tab" />
<p>First, click the <strong>Upload Tab</strong>.</p>

<img class="steps" src="images/publish/service/blip-03.jpg" alt="Upload" />
<p>Next, name your video and give it a concise description.</p>

<img class="steps" src="images/publish/service/blip-04.jpg"alt="Browse" />
<p>Now, <strong>Browse</strong> for <strong>Your File</strong>. The thumbnail is optional and will be automatically generated if you leave this space blank.</p>
</div> <!-- END STEP 2 -->

<div class="steps">
<h2><span class="number">3</span> A Few Important Details</h2>

<img class="steps" src="images/publish/service/blip-05.jpg"alt="Choose a License" />
<p>We think licensing is so important, <a href="license/license-intro.php">we wrote a whole chapter on it</a>.</p>
<p>blip.tv makes it exceptionally easy to mark your work with any license you see fit (check out the <a class="wikilink" href="license/cc.php">Creative Commons</a> licenses).</p>

<img class="steps" src="images/publish/service/blip-06.jpg"alt="Tag It" />
<p>Tagging is important for search purposes; use keywords and terms that viewers might look for.</p>

</div> <!-- END STEP 3 -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Advanced Publishing Capabilities</h2>
<p>blip.tv has a lot of advanced upload features. For example, blip will automatically cross-post your video to a blog, a myspace account, or a variety of other places. They also have tools that make promoting your videos easier.</p>
<p>blip.tv's <a class="wikilink" href="http://blip.tv/learning/">learning center</a> covers many of their advanced features.</p>
</div> <!-- END CALLOUT -->

<div class="interior">

<div class="steps">
<h2><span class="number">4</span> Upload It!</h2>

<img class="steps" src="images/publish/service/blip-07.jpg" alt="Upload It" />
<p>Hit upload.</p>

<img class="steps" src="images/publish/service/blip-08.jpg"alt="Wait" />
<p>Now, wait for your video to fully upload (don't reload or close this browser window).</p>

</div> <!-- END STEP 4 -->
	
