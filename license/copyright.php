<h1 class="pagetitle">Chapter 4.3: Copyright & Fair Use</h1>
<p>Copyright law was originally intended as a temporary monopoly on copying; an incentive to encourage authors and inventors to produce cultural works. Its scope and influence have grown considerably since 1787.</p>

<p>Computers make copies each time a piece of media is shared or sent. This characteristic has collided with the modern copyright system and raised many new questions, philosophies, and issues.</p> 

<p>This section quickly covers the basics of copyright, fair use, and the public domain.</p>
