<h2>What is Copyright Infringement?</h2>
<div class="detailz">
<img class="right lined" src="images/license/notmickey.gif" style="margin-top:10px;" />
<p>Copyright infringement occurs when a person distributes, copies or incorporates (into their work) part or all of a copyrighted work (song, video, text, etc), without permission from the rights holder.</p> 
<p>For example, let's say you create a video about poverty in Africa and use a U2 song in the background; if you don't have permission from Universal Music Group, you are technically violating copyright. The person who created the song, video, or book is not necessarily the rights holder, as is often the case in the music industry.</p>
<p>However, you can <em>legally</em> incorporate portions of, and sometimes entire, copyrighted works into your own creations, due to a part of the US copyright law called <em>fair use</em> (see below).</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Fair Use: Legally Use Copyrighted Material!</h2>
<p>Fair use is an important set of exceptions to copyright law. They make it legal for people like you to use parts, or sometimes all, of a copyrighted work for the purposes of: criticism, comment, news reporting, teaching, scholarship, or research.</p>
<p>The Center for Social Media at American University created a the <a class="wikilink" href="http://www.centerforsocialmedia.org/resources/publications/fair_use_in_online_video/">Code of Best Practices in Fair Use for Online Video</a> (the <a class="wikilink" href="http://www.centerforsocialmedia.org/files/pdf/online_best_practices_in_fair_use.pdf">PDF Version</a> is laid out better). This booklet can help you understand if and when your use of copyrighted works should be considered fair. It's a great resource for anyone who remixes video and posts it to the internet.</p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h2>When Copyright Terms End: The Public Domain</h2>
<div class="details">
<p>A work becomes completely and freely usable when the copyright term lapses; currently that's usually after the author has died, plus 70 years. A lot of public domain media can be downloaded at <a class="wikilink" href="http://www.archive.org/index.php">Archive.org</a>.</p>

<p>Unfortunately, copyright terms have  increased sharply in the past six decades, making less work fall into the public domain. James Boyle, a law professor at Duke University, wrote the book, <em>The Public Domain: Enclosing the Commons of the Mind</em>, which can be <a class="wikilink" href="http://thepublicdomain.org">downloaded and read for free</a> (it's licensed with Creative Commons). Boyle presents an engaging and interesting case for an expanded public domain and copyright laws more in step with the digital environment.</p>
</div> <!-- END DETAILS -->

<h2>Creative Commons and Copyright</h2>
<div class="details">
<p>US copyright law is complex, especially regarding music and movies, and rights holders are sometimes litigious and aggressive. As a result, building upon other people's work can seem overwhelming and even scary.</p>
<p>Creative Commons removes a lot of risk and mystery, and is part of why we <a href="./license/cc.php">encourage using CC licenses</a> on your videos, and likewise building upon other people's CC'ed works.</p>
</div> <!-- END DETAILS -->

