<h1 class="pagetitle">Chapter 4.2: Creative Commons</h1>
<p>Creative Commons is a way to let everyone know how they can and can't use your video (or multiple videos). To use it, simply state that a video is CC, along with the parameters of the license.</p>

<p>You can put your CC license in any <em>or</em> all of the following places: in the video itself, in the tags, on your blog or website, anywhere you want!</p>
