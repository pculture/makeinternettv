<h1>How do I use Creative Commons?</h1>

<div class="details">
<img class="left" src="images/license/cc.png" alt="How do I use CC?" />
<p>A CC license helps you express safely and legally how you want your work to be used.</p>
<p>Creative Commons offers six licenses ranging from very permissive (Attribution) to more restrictive (Attribution-NonCommercial-NoDerivatives). Below are explanations of each CC attribute, but for making your first license, try the Creative Commons <a class="wikilink" href="http://creativecommons.org/license/">License Chooser</a>.</p> 
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout expert">
<h2>Mini-Case Study: The Lonely Island & Creative Commons</h2>
<img class="co illus" src="images/license/andy.jpg" alt="Andy Samberg" />
<p>Andy Samberg, Jorma Taccone and Akiva Schaffer, who have all been involved in Saturday Night Live, began as <a class="wikilink" href="http://www.thelonelyisland.com/">The Lonely Island</a>, a small comedy collective based in LA. They <a class="wikilink" href="http://creativecommons.org/weblog/entry/7065">licensed many of their videos using Creative Commons</a> (BY-NC-SA). While their success is due largely to their comedic talent, open licensing helped in get their videos and names spread further.</p>
<p class="minordetails">Image by: <a href="http://flickr.com/photos/jackcheng/149897023/">jackcheng</a> (cc-nc-nd)</p> 
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h1>Step 1: Mix a License</h1>

<h2>Attribution (BY)</h2>

<div class="details">
<img class="right" src="images/license/cc-by.png" alt="Attribution" />
<p>Attribution is the basic component of all CC licenses and merely requires anyone using, sharing, or re-mixing your videos to give you credit in the way in which you specify.</p>
<br />
</div> <!-- END DETAILS -->

<h2>ShareAlike (SA)</h2>

<div class="details">
<img class="left" src="images/license/cc-sa.png" alt="Share Alike" />
<p>ShareAlike licenses requires that if a person modifies your video, they <em>must</em> share the resulting media under the exact same CC license. This is license is great because it encourages people to use your videos and to share their work in the same capacity!</p>
</div> <!-- END DETAILS -->

<h2>Non-Commercial (NC)</h2>

<div class="details">
<img class="right" src="images/license/cc-nc.png" alt="Non-Commercial" />
<p>Adding the non-commercial stipulation means that a person can't sell your videos or any resulting media that incorporates your work. This might discourage websites using <a class="wiki" href="http://en.wikipedia.org/wiki/AdSense">Google Adsense</a> from featuring your videos, and can discourage others from further reusing your work</p>
</div> <!-- END DETAILS -->

<h2>NoDerivatives (ND)</h2>

<div class="details">
<img class="left" src="images/license/cc-nd.png" alt="No Derivatives" />
<p>NoDerivatives restricts people from modifying your work. Be aware that NoDerivatives very much limits what people can do with your work downstream and will discourage people from reusing your work creatively. Mashups are a great example of how video and culture can be remixed and refined (example of mashup, <a class="wikilink" href="http://www.youtube.com/watch?v=L6TuMhYg89E&feature=channel_page">Girl Talk Video</a>). Despite this, you may want to consider NoDerivatives licenses if you are making video that contains footage you don't want remixed, such as interviews or other sensitive content.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Using Other Peoples' Creative Commons Works</h2>
<p>If someone else has marked their music, art, or video as Creative Commons, you can use it <em>without even asking them</em>. Just make sure you follow the conditions of the license deed (the link to the license itself) and give them credit. This is why CC is so cool!</p> 
<p>Find more CC'ed media at the <a class="wikilink" href="http://wiki.creativecommons.org/Content_Curators">CC Content Directories</a></p>
</div> <!-- END CALLOUT -->

<div class="clear"> </div>

<div class="interior">

<h1>Step 2: Put It All Together</h1>

<div class="details">
<img class="right" src="images/license/ccbadge-by-sa.png" alt="CC-BY-SA" />
<p>If you're making a video, all you've got to do is declare that it's licensed under one of the six CC licenses (<a class="wikilink" href="http://creativecommons.org/license/">select your license here</a>) and put the badge in your video. Creative Commons even has high resolution and vector images for you to use in <a class="wikilink" href="http://creativecommons.org/about/downloads/">any medium available for download here.</p>

<p>For example, this site is pretty much 100% Creative Commons. We simply attached a notice at the bottom of each page, declaring the site CC-BY-SA.</p>

<p>This means that any 3rd party can share or remix any part of our site into their work, as long as they give us credit and share the resulting work under the same terms. Simple and effective.</p>
</div> <!-- END DETAILS -->

</div> <!-- END INTERIOR -->

<div class="callout important">
<h2>Irrevocability</h2>
<p>Once you release a copy of your work with a CC license, you cannot 'undo' the license. However, you always retain full-rights to the original, and can re-release subsequent versions (of the work) without a CC license.</p> 
<p>This might sound scary, but it protects everyone using CC licenses. In other words, if you were to use a song (that someone else released as CC) for the theme in every episode of your video blog, it will never be un-CC'ed.</p>
</div> <!-- END CALLOUT -->

